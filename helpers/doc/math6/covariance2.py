import numpy as np;
from matplotlib import pyplot as plt;

############################## 方差和样本方差 ##############################
x = np.array( [ 1, 8, 6, 4, 10, 7 ] );

# 将 x 均值转为 0;
x = x - x.mean();
# [-5.  2.  0. -2.  4.  1.]

# x 方差;
# σ² = √( ∑ⁿᵢ₌₁( ( x - μ )² ) / n );
#    = ( (-5)² + (2)² + (0)² + (-2)² + (4)² + (1)² ) / 6
#    = ( 25 + 4 + 0 + 4 + 16 + 1 ) / 6
#    = 50 / 6 = 8.333333333333334

# x 样本方差;
# S² = ∑ⁿᵢ₌₁( ( xᵢ - x` )² ) / n;
#    = 50 / 5 = 10

# np.var 返回数组元素的方差;
np.var( x );
# 8.333333333333334

np.dot( x.T, x ) / len( x );
# 8.333333333333334

# np.var 调整 ddof 自由度减少量得到样本方差;
np.var( x, ddof = 1 );
# 10

np.dot( x.T, x ) / ( len( x ) - 1 );
# 10

############################## 协方差和协方差矩阵 ##############################
x = np.array( [ 1, 2, 3, 4, 5, 6 ] );
y = np.array( [ 2, 4, 6, 8, 10, 12 ] );

# x 去均值
x = x - x.mean();
# [-2.5 -1.5 -0.5  0.5  1.5  2.5]

# y 去均值
y = y - y.mean();
# [-5. -3. -1.  1.  3.  5.]

xy = np.vstack( ( x, y ) )
# [ [ -2.5 -1.5 -0.5  0.5  1.5  2.5 ]
#   [ -5.  -3.  -1.   1.   3.   5.  ] ]

# cov( x, y ) = ∑ⁿᵢ₌₁( ( xᵢ - μᵪ ) * ( yᵢ - μᵧ ) ) / ( n / 1 );
#             = ( ( -2.5 * -5 ) + ( -1.5 * -3 ) + ( -0.5 * -1 ) + ( 0.5 * 1 ) + ( 1.5 * 3 ) + ( 2.5 * 5 ) ) / ( 6 - 1 )
#             = 35 / 5 = 7

# np.cov 返回协方差矩阵
np.cov( xy );
# [ [ 3.5  7. ]
#   [ 7.  14. ] ]

# 主对角线上 x 方差
np.var( x, ddof = 1 );
# 3.5

# 主对角线上 y 方差
np.var( y, ddof = 1 );
# 14.0

# 根据公式计算 x, y 协方差
np.dot( x.T, y ) / ( len( x ) - 1 );
# 7.0

# 根据公式计算 x 协方差
np.dot( x.T, x ) / ( len( x ) - 1 );
# 3.5

# 根据公式计算 y 协方差
np.dot( y.T, y ) / ( len( y ) - 1 );
# 14.0

############################## 将数据集转化为协方差矩阵 ##############################
np.random.seed( 0 );
x = np.random.random( size = ( 4, 3 ) );
# [ [ 0.5488135  0.71518937 0.60276338 ]
#   [ 0.54488318 0.4236548  0.64589411 ]
#   [ 0.43758721 0.891773   0.96366276 ]
#   [ 0.38344152 0.79172504 0.52889492 ] ]

x = x - x.mean( axis = 0 );
# [ [  0.07013215  0.00960382 -0.08254042 ]
#   [  0.06620183 -0.28193075 -0.03940968 ]
#   [ -0.04109414  0.18618745  0.27835897 ]
#   [ -0.09523984  0.08613949 -0.15640887 ] ]

# rowvar: False 表示列表示特征, True 表示行表示特征
np.cov( x, rowvar = False );
# [ [  0.00668685 -0.01128197 -0.00164677 ]
#   [ -0.01128197  0.04055432  0.01622402 ]
#   [ -0.00164677  0.01622402  0.03677116 ] ];

# 根据公式将数据集转化为协方差矩阵: ( 1 / ( m - 1 ) ) * xᵀ * x
np.dot( x.T, x ) / ( len( x ) - 1 );
# [ [  0.00668685 -0.01128197 -0.00164677 ]
#   [ -0.01128197  0.04055432  0.01622402 ]
