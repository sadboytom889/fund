# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 自由度																																	|
# | 定义: 计算某一统计量时, 取值不受限制的变量个数;																									|
# |																																			|
# | 例:																																		|
# | 计算 x, y, z 的平均数时使用 ( x + y + z ) / 3; 3 就是自由度, 因为 x, y, z 这 3 个变量都能为平均数提供信息;											|
# | 而加上条件 x + y + z = 10 后, 其自由度由 3 变为 2, 因为已知任意两个变量可得出第三个变量如已知 x, y 可以计算 z;										|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 方差																																		|
# | 衡量随机变量或一组数据的离散程度的度量( 离平均的平方距离的平均 ), 各个数据与均值的差的平方和再求平均;													|
# | 公式 σ² = ∑ⁿᵢ₌₁( ( xᵢ - μ )² ) / n;																										|
# | x: 测量值;																																|
# | μ: 均值;																																	|
# | σ²: 方差;																																|
# | n: 次数;																																	|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 样本方差																																	|
# | 样本方差用抽样的方式估计方差的值, 适用于个数过于庞大或需要破坏性实验的数据, 仅能采取抽样的方式来估计数据方差;												|
# | 公式 S² = ∑ⁿᵢ₌₁( ( xᵢ - x` )² ) / ( n - 1 );																								|
# | x: 样本值;																																|
# | x`: 样本均值;																																|
# | S²: 样本方差;																																|
# | n: 次数;																																	|
# |																																			|
# | 注意:																																	|
# | 分母为 n - 1 而不是 n;																														|
# |																																			|
# | 推导:																																	|
# | 若分母为 n, 则:																															|
# | S² = ∑ⁿᵢ₌₁( ( xᵢ - x` )² ) / n;																											|
# | σ² = ∑ⁿᵢ₌₁( ( xᵢ - μ )² ) / n;																											|
# |																																			|
# | 对于 S² 分子部分, 样本范围内 x` 的取值来计算 ∑ⁿᵢ₌₁( ( xᵢ - x` )² ) 必小于 ∑ⁿᵢ₌₁( ( xᵢ - μ )² ),												|
# | 并且 x` 这样的取值必然是样本范围内计算 ∑ⁿᵢ₌₁( ( xᵢ - x` )² ) 的最小值:																			|
# | ∑ⁿᵢ₌₁( ( xᵢ - x` )² ) < ∑ⁿᵢ₌₁( ( xᵢ - μ )² );																							|
# |																																			|
# | 故造成若分母为 n 则样本方差 S² 对于方差 σ² 的估计必然偏小, 使用自由度的方式理解分母部分:																|
# | 方差 σ² 的分母自由度为总数据个数 n; 样本方差 S² 的分母自由度为 n - 1; 因为 S² 公式中存在 x`, 即:													|
# | ( x₁ - x` )² + ( x₂ - x` )² + ( x₃ - x` )² + ... + ( xₙ - x` )² 中的某一项因为 x` 的存在并不能为式子提供自由度;									|
# |																																			|
# | 特例:																																	|
# | 若 x` 为 0, 则样本方差表示为:																												|
# | S² = ( 1 / ( n - 1 ) ) * ∑ⁿᵢ₌₁( xᵢ );																									|
# |																																			|
# | 使用矩阵方式表示:																															|
# | x = [ x₁, x₂, x₃, ..., xₙ ];																												|
# | S² = ( 1 / ( n - 1 ) ) * xᵀ * x;																										|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 标准差																																	|
# | 标准差是数值分散的测量, 是方差的平方根, 通过方差的平方根来观察一组数字离平均值有多远;																	|
# | 公式 σ = √( ∑ⁿᵢ₌₁( ( xᵢ - μ )² ) / n );																									|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 协方差																																	|
# | 标准差和方差一般用来描述一维数据, 协方差是一种用来度量两个或多个随机变量变化步调是否一致的统计量;														|
# | 公式: cov( x, y ) = ∑ⁿᵢ₌₁( ( xᵢ - μᵪ ) * ( yᵢ - μᵧ ) ) / ( n / 1 );																		|
# |																																			|
# | 解释:																																	|
# | 若结果为正值, 则说明两者正相关; 因若 xᵢ 大于或小于 x 均值时, y 亦是如此, 则式子为正数;																|
# | 若结果为负值, 则说明两者负相关; 因若 xᵢ 大于或小于 x 均值时, y 却相反, 则式子为负数;																|
# | 若结果为 0, 则两者之间没有关系; 因若 xᵢ 大于或小于 x 均值时, y 都不变, 则式子为 0;																	|
# | 累加后得到的结果反应这两段数据是否同步调相似;																									|
# |																																			|
# | 特例 1:																																	|
# | 若 x 就是 y, 则协方差也就是方差;																												|
# |																																			|
# | 特例 2:																																	|
# | 若 μᵪ 与 μᵧ 都为 0, 则:																													|
# | cov( x, y ) = ( 1 / ( n - 1 ) ) * ∑ⁿᵢ₌₁( xᵢyᵢ );																						|
# |																																			|
# | 转化为矩阵形式:																															|
# | x = [ x₁, x₂, x₃, ..., xₙ ];																												|
# | y = [ y₁, y₂, y₃, ..., yₙ ];																												|
# | cov( x, y ) = ( 1 / ( n - 1 ) ) * xᵀ * y;																								|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 协方差矩阵																																|
# | 协方差矩阵用来存储多个随机变量两两之间的协方差;																									|
# |																																			|
# | 例:																																		|
# | 存在 n 个随机变量 x₁, x₂, x₃, ..., xₙ, 则协方差矩阵:																							|
# |     --     --                            --						--																		|
# |     |  x₁	|                            |	C₁₁	C₁₂	C₁₃	...	C₁ₙ	 |																		|
# |     |  x₂	|   --                 --    |	C₂₁	C₂₂	C₂₃	...	C₂ₙ	 |;																		|
# | C = |  x₃	| * |  x₁ x₂ x₃ ... xₙ	|  = |	C₃₁	C₃₂	C₃₃	...	C₃ₙ	 |																		|
# |     |  ...	|   --                 --    |	...					 |																		|
# |     |  xₙ	|                            |	Cₙ₁	Cₙ₂	Cₙ₃	...	Cₙₙ	 |																		|
# |     --     --                            --						--																		|
# | 分析得到:																																	|
# | 1: 协方差主对角线元素是各随机变量的方差;																										|
# | 2: 因特征 a 与 b 的协方差等价于 b 与 a 的协方差, 故协方差矩阵为对称矩阵;																			|
# |																																			|
# | 特例:																																	|
# | 假设将 m 个样本, 每个样本 n 个特征的数据集使用 xₘₙ 矩阵表示, 则:																					|
# |      --						--																											|
# |      |	x₁₁	x₁₂	x₁₃	...	x₁ₙ	 |																											|
# |      |	x₂₁	x₂₂	x₂₃	...	x₂ₙ	 |																											|
# | x =  |	x₃₁	x₃₂	x₃₃	...	x₃ₙ	 |;																											|
# |      |	...					 |																											|
# |      |	xₘ₁	xₘ₂	xₘ₃	...	xₘₙ	 |																											|
# |      --						--																											|
# | 即: 行向量代表一个样本的所有特征, 列向量代表所有样本的一个特征;																					|
# |																																			|
# |          --						--   --						--																			|
# |          |	x₁₁	x₂₁	x₃₁	...	xₘ₁	 |   |	x₁₁	x₁₂	x₁₃	...	x₁ₙ	 |																			|
# |          |	x₁₂	x₂₂	x₃₂	...	xₘ₂	 |   |	x₂₁	x₂₂	x₂₃	...	x₂ₙ	 |																			|
# | xᵀ * x = |	x₁₃	x₂₃	x₃₃	...	xₘ₃	 | * |	x₃₁	x₃₂	x₃₃	...	x₃ₙ	 |																			|
# |          |	...					 |   |	...					 |																			|
# |          |	x₁ₙ	x₂ₙ	x₃ₙ	...	xₘₙ	 |   |	xₘ₁	xₘ₂	xₘ₃	...	xₘₙ	 |																			|
# |          --						--   --						--																			|
# |																																			|
# |         --                                                                                                               --				|
# |         |                             --   --                            --   --                                 --    -- |				|
# |         |                             | x₁₁	|                            | x₁₂ |                                 |  x₁ₙ	| |				|
# |         |   --                   --   | x₂₁	|  --                   --   | x₂₂ |       --                   --   |  x₂ₙ	| |				|
# |         |   | x₁₁ x₂₁ x₃₁ ... xₘ₁ | * | x₃₁	|, | x₁₁ x₂₁ x₃₁ ... xₘ₁ | * | x₃₂ |, ..., | x₁₁ x₂₁ x₃₁ ... xₘ₁ | * |  x₃ₙ	| |				|
# |         |   --                   --   | ...	|  --                   --   | ... |       --                   --   |  ...	| |				|
# |         |                             | xₘ₁	|                            | xₘ₂ |                                 |  xₘₙ	| |				|
# |         |                             --   --                            --   --                                 --    -- |				|
# |         |                                                                                                                 |				|
# |         |                             --   --                            --   --                                 --    -- |				|
# |         |                             | x₁₁	|                            | x₁₂ |                                 |  x₁ₙ	| |				|
# |         |   --                   --   | x₂₁	|  --                   --   | x₂₂ |       --                   --   |  x₂ₙ	| |				|
# |         |   | x₁₂ x₂₂ x₃₂ ... xₘ₂ | * | x₃₁	|, | x₁₂ x₂₂ x₃₂ ... xₘ₂ | * | x₃₂ |, ..., | x₁₂ x₂₂ x₃₂ ... xₘ₂ | * |  x₃ₙ	| |				|
# |       = |   --                   --   | ...	|  --                   --   | ... |       --                   --   |  ...	| |				|
# |         |                             | xₘ₁	|                            | xₘ₂ |                                 |  xₘₙ	| |				|
# |         |                             --   --                            --   --                                 --    -- |				|
# |         |                                                                                                                 |				|
# |         |   ...                                                                                                           |				|
# |         |                                                                                                                 |				|
# |         |                           --   --                             --   --                                  --    -- |				|
# |         |                           | x₁₁ |                             | x₁₂ |                                  |  x₁ₙ	| |				|
# |         |   --                 --   | x₂₁ |  --                    --   | x₂₂ |       --                    --   |  x₂ₙ	| |				|
# |         |   | x₁ₙ x₂ₙ x₃ₙ ... xₘₙ	| * | x₃₁ |, |  x₁ₙ x₂ₙ x₃ₙ ... xₘₙ	| * | x₃₂ |, ..., |   x₁ₙ x₂ₙ x₃ₙ ... xₘₙ	 | * |  x₃ₙ	| |				|
# |         |   --                 --   | ... |  --                    --   | ... |       --                    --   |  ...	| |				|
# |         |                           | xₘ₁ |                             | xₘ₂ |                                  |  xₘₙ	| |				|
# |         |                           --   --                             --   --                                  --    -- |				|
# |         --                                                                                                               --				|
# |                     --  --																												|
# |                     | x₁ |																												|
# |                     | x₂ |																												|
# | 令每个特征为向量 xⱼ =	| x₃ |, 则:																											|
# |                     | .. |																												|
# |                     | xₘ |																												|
# |                     --  --																												|
# |          --														--																		|
# |          |	x₁ᵀ * x₁	x₁ᵀ * x₂	x₁ᵀ * x₃	...		x₁ᵀ * xₙ	 |																		|
# |          |	x₂ᵀ * x₁	x₂ᵀ * x₂	x₂ᵀ * x₃	...		x₂ᵀ * xₙ	 |																		|
# | xᵀ * x = |	x₃ᵀ * x₁	x₃ᵀ * x₂	x₃ᵀ * x₃	...		x₃ᵀ * xₙ	 |;																		|
# |          |	...													 |																		|
# |          |	xₙᵀ * x₁		xₙᵀ * x₂		xₙᵀ * x₃		...		xₙᵀ * xₙ	 |																		|
# |          --														--																		|
# |																																			|
# | 矩阵除以 m - 1 得到:																														|
# |                              --																										--	|
# |                              |	( 1 / ( m - 1 ) ) * x₁ᵀ * x₁	( 1 / ( m - 1 ) ) * x₁ᵀ * x₂	...		( 1 / ( m - 1 ) ) * x₁ᵀ * xₙ	 |	|
# |                              |	( 1 / ( m - 1 ) ) * x₂ᵀ * x₁	( 1 / ( m - 1 ) ) * x₂ᵀ * x₂	...		( 1 / ( m - 1 ) ) * x₂ᵀ * xₙ	 |	|
# | ( 1 / ( m - 1 ) ) * xᵀ * x = |	( 1 / ( m - 1 ) ) * x₃ᵀ * x₁	( 1 / ( m - 1 ) ) * x₃ᵀ * x₂	...		( 1 / ( m - 1 ) ) * x₃ᵀ * xₙ	 |;	|
# |                              |	...																									 |	|
# |                              |	( 1 / ( m - 1 ) ) * xₙᵀ * x₁		( 1 / ( m - 1 ) ) * xₙᵀ * x₂		...		( 1 / ( m - 1 ) ) * xₙᵀ * xₙ	 |	|
# |                              --																										--	|
# |																																			|
# | 该矩阵每个特征列 xⱼ 的均值为 0 时的协方差公式:																									|
# | cov( x, y ) = ( 1 / ( n - 1 ) ) * ∑ⁿᵢ₌₁( xᵢyᵢ );																						|
# |																																			|
# | 得到:																																	|
# | 若给定数据集 x, 且每个特征均值都为 0, 协方差矩阵可表示为: ( 1 / ( m - 1 ) ) * xᵀ * x;															|
