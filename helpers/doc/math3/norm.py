# |---------------------------------------------------------------------------------|
# | 范数:																			|
# | 集合通过一种映射关系得到另外一个集合, 存在的意义是实现比较.								|
# | 因为向量与矩阵无法直接比较大小, 而通过范数转换为可比较的实数, 范数的本质是距离;				|
# |																					|
# | 向量范数:																			|
# | L1范数: 绝对值之和;																|
# | 	   ||x||₁ = ∑ⁿᵢ₌₁( | xᵢ | );												|
# |																					|
# | 若 x = ( 1, 4, 3, −1 );															|
# | 则 ||x||₁ = | x₁ | + | x₂ | + | x₃ | + | x₄ |									|
# | 		 = | 1 | + | 4 | + | 3 | + | −1 |										|
# | 		 = 1 + 4 + 3 + 1														|
# | 		 = 9;																	|
# |																					|
# | L2范数: 欧几里得范数;																|
# | 	   ||x||₂ = √( ∑ⁿᵢ₌₁( xᵢ² ) ) or ||x||₂ = ( ∑ⁿᵢ₌₁( xᵢ² ) )¹⁄²;				|
# |																					|
# | 若 x = ( 1, 4, 3, −1 );															|
# | ||x||₂ = √( 1² + 4² + 3² + -1² )												|
# | 	   = √( 1 + 16 + 9 + 1 )													|
# | 	   = √( 27 );																|
# |																					|
# | Ln范数: 向量元素绝对值的 n 次方和的 1 / n 次幂;										|
# | 	   ||x||ₙ = ( ∑ⁿᵢ₌₁( xᵢ² ) )¹⁄ⁿ;												|
# |																					|
# | 若 x = ( 1, 4, 3, −1 );															|
# | ||x||₃ = √( 1³ + 4³ + 3³ + -1³ )												|
# | 	   = √( 1 + 64 + 27 + -1 )													|
# | 	   = ³√( 91 );																|
# |																					|
# | L0范数: 向量中非零元素的数量;														|
# | 		||x||₀ = ∑ⁿᵢ₌₁( xi ≠ 0 );												|
# |																					|
# | 若 x = ( 1, 4, 3, −1 );															|
# | ||x||₀ = 4;																		|
# |																					|
# | L+∞范数: 所有向量元素绝对值中的最大值;												|
# | 		||x||+∞ = max( x₀, x₁, ..., xᵢ );										|
# |																					|
# | 若 x = ( 1, 4, 3, −1 );															|
# | ||x||+∞ = 4;																	|
# |																					|
# | L-∞范数: 所有向量元素绝对值中的最小值;												|
# | 		||x||-∞ = min( x₀, x₁, ..., xᵢ );										|
# |																					|
# | 若 x = ( 1, 4, 3, −1 );															|
# | ||x||-∞ = -1;																	|
# |																					|
# |																					|
# | 矩阵范数:																			|
# |																					|
# | A矩阵( m * n )形如:																|
# | A₁₁ A₁₂ A₁₃ ... A₁ₙ																|
# | A₂₁ A₂₂ A₂₃ ... A₂ₙ																|
# | A₃₁ A₃₂ A₃₃ ... A₃ₙ																|
# | ...																				|
# | Aₘ₁ Aₘ₂ Aₘ₃ ... Aₘₙ																|
# |																					|
# | L1范数: 列和范数, 矩阵列向量绝对值之和的最大值;										|
# | 	   ||A||₁ = max( ∑ᵐᵢ₌₁( | aᵢ₁ | ), ..., ∑ᵐᵢ₌₁( | aᵢₙ | ) );					|
# |																					|
# | 若 A = [ [ 1, 4, 3 ],															|
# | 		 [ 2, 7, 5 ],															|
# | 		 [ 6, 1, 3 ] ];															|
# |																					|
# | ||A||₁ = max( ( 1 + 2 + 6 ), ( 4 + 7 + 1 ), ( 3 + 5 + 3 ) )						|
# | 	   = max( 9, 12, 11 )														|
# | 	   = 12;																	|
# |																					|
# | L∞范数: 行和范数, 矩阵行向量绝对值之和的最大值;										|
# | 	   ||A||₁ = max( ∑ⁿᵢ₌₁( | a₁ᵢ | ), ..., ∑ⁿᵢ₌₁( | aₘᵢ | ) );					|
# |																					|
# | 若 A = [ [ 1, 4, 3 ],															|
# | 		 [ 2, 7, 5 ],															|
# | 		 [ 6, 1, 3 ] ];															|
# |																					|
# | ||A||∞ = max( ( 1 + 4 + 3 ), ( 2 + 7 + 5 ), ( 6 + 1 + 3 ) )						|
# | 	   = max( 8, 14, 10 )														|
# | 	   = 14;																	|
# |																					|
# | LF范数: 矩阵元素绝对值的平方和再开平方;												|
# | 	   ||A||f = √( ∑( ∑( aₘₙ )² ) );												|
# |																					|
# | 若 A = [ [ 1, 4, 3 ],															|
# | 	    [ 2, 7, 5 ],															|
# | 	    [ 6, 1, 3 ] ];															|
# |																					|
# | ||A||f = 1² + 4² + 3² + 2² + 7² + 5² + 6² + 1² + 3²								|
# | 	   = 1 + 16 + 9 + 4 + 49 + 25 + 36 + 1 + 9									|
# | 	   = 5 * √(6);																|
# | 	   = 12.2474487139															|
# |																					|
# | L2范数: Aᵀ * A 矩阵的最大特征值开平方;												|
# | 	  √( max( λ₁, λ₂, ... , λₙ ) );												|
# |																					|
# | A = [ [ 1, 1 ],      Aᵀ = [ [ 1, 0, -2 ],    Aᵀ * A = [ [ 5, -1 ],				|
# | 	 [ 0, 1 ],      	   [ 1, 1, 1 ] ];   		 [ -1, 3 ] ];				|
# | 	 [ -2, 1 ] ];																|
# |																					|
# | 求 [ [ 5, -1 ],  的特征值															|
# | 	[ -1, 3 ] ]																	|
# |																					|
# | [ [ 5 - λ, -1 ],  = 0															|
# |   [ -1, 3 - λ ] ]																|
# |																					|
# | ( 5 - λ )( 3 - λ ) = 1															|
# | λ² - 8 * λ + 14 = 0;															|
# | λ₁ = 4 + √2																		|
# | λ₂ = 4 - √2																		|
# |																					|
# | √( max( 4 + √2, 4 - √2 ) ) = 2.3268462696046543									|
