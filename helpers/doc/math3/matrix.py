# | 单位矩阵:																			|
# | 单位矩阵的特征值皆为 1, 任何向量都是单位矩阵的特征向量									|
# | --   --   -- --   --             --   -- --										|
# | | 1 0 | * | 3 | = | 1 * 3 + 0 * 5 | = | 3 |										|
# | | 0 1 |   | 5 |   | 0 * 3 + 1 * 5 |   | 5 |										|
# | --   --   -- --   --             --   -- --										|
# |---------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------|
# | 矩阵行列式:																		|
# | 1: n 阶行列式由 n 个 n 维向量为邻边构成的 n 维图形的体积(2维为面积)						|
# |																					|
# | 2: 矩阵一定要是方形矩阵																|
# |																					|
# | 3: 2 * 2 矩阵行列式																|
# | A: --   --      |A| = ad - bc													|
# |    | a b |																		|
# |    | c d |																		|
# |    --   --																		|
# |																					|
# | 3: 3 * 3 矩阵行列式																|
# | A: --     --    |A| = a( ei - fh ) - b( di - fg ) + c( dh - eg )				|
# |    | a b c |        --   --        --   --        --   --						|
# |    | d e f |    a * | e f |  - b * | d f |  - c * | d e |						|
# |    | g h i |        | h i |        | g i |        | g h |						|
# |    --     --        --   --        --   --        --   --						|
# |																					|
# | 4: 4 * 4 矩阵行列式																|
# |  --       --																	|
# |  | a b c d |        --     --       --     --       --     --       --     --	|
# |  | e f g h |    a * | f g h | - b * | e g h | + c * | e f h | - d * | e f g |	|
# |  | i g k l |        | g k l |       | i k l |       | i g l |       | i g k |	|
# |  | m n o p |        | n o p |       | m o p |       | m n p |       | m n o |	|
# |  --       --        --     --       --     --       --     --       --     --	|
# |																					|
# | 5:																				|
# | 1): a 乘以不在 a 的行或列上的 2 * 2 矩阵的行列式										|
# | 2): b 乘以不在 b 的行或列上的 2 * 2 矩阵的行列式										|
# | 3): c 乘以不在 c 的行或列上的 2 * 2 矩阵的行列式										|
# | 4): 把 1, 2, 3 结果加在一起，符号遵循 + - + - 的规律									|
# |---------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------------------|
# | 矩阵特征值:																					|
# | A 为 n阶方阵，若数 λ 和 n维非零列向量 x 使关系式 A x = λ x 成立									|
# | 则数 λ 称为矩阵 A 特征值;																		|
# | 非零向量 x 称为 A 的对应于特征值 λ 的特征向量;														|
# |																								|
# | 例如:																						|
# | 向量x -- -- 与 -- -- 在矩阵 --   -- 的作用下只在大小或正负方向上发生变化或变成零向量( λ = 0 )			|
# |      | 5 |    | 3 |       | 6 3 | 使关系式 A x = λ x 成立										|
# |      | 1 |    | 4 |       | 4 7 | 															|
# |      -- --    -- --       --   -- 															|
# | --   --   -- --   --             --   --  --												|
# | | 6 3 | * | 5 | = | 6 * 5 + 3 * 1 | = | 33 |												|
# | | 4 7 |   | 1 |   | 4 * 5 + 7 * 1 |   | 27 |												|
# | --   --   -- --   --             --   --  --												|
# |																								|
# | --   --   -- --   --             --   --  --        -- --									|
# | | 6 3 | * | 3 | = | 6 * 3 + 3 * 4 | = | 30 | = 10 * | 3 | -> 10 是 A 的特征值					|
# | | 4 7 |   | 4 |   | 4 * 3 + 7 * 4 |   | 40 |        | 4 | -> [ 3, 4 ] 是 A 对应 10 的特征向量	|
# | --   --   -- --   --             --   --  --        -- --									|
# |																								|
# | 若存在特征向量, 则 A x = λ x -> ( A - λE ) x = 0 成立											|
# | 则在矩阵 A: --                --																|
# | 		     | A₁₁ - λ A₁₂      |															|
# | 		     | A₂₁     A₂₂ -  λ |															|
# | 		     --                --															|
# | 存在 λ 使得 ( A₁₁ - λ ) * ( A₂₂ - λ ) - A₂₁ * A₁₂ = 0;										|
# |																								|
# | 例如:																						|
# | --   --    --           --																	|
# | | 5 3 | -> | 5 - λ     3 | -> ( 5 - λ ) * ( 5 - λ ) = 3 * 3;								|
# | | 3 5 |    | 3     5 - λ | -> λ = 8 或 λ = 2;												|
# | --   --    --           --																	|
# |																								|
# |    λ = 8 带入 ( A - λE ) x = 0 得到															|
# |    --           --   --  --   -- --															|
# | -> | 5 - 8     3 | * | x₁ | = | 0 |															|
# |    | 3     5 - 8 |   | x₂ |   | 0 |															|
# |    --           --   --  --   -- --															|
# |																								|
# |    --      --   --  --   -- --																|
# | -> | -3   1 | * | x₁ | = | 0 |																|
# |    |  3  -1 |   | x₂ |   | 0 |																|
# |    --      --   --  --   -- --																|
# |																								|
# | -> -3 * x₁ + 3 * x₂ = 0;																	|
# |    -3 * x₁ - 3 * x₂ = 0;																	|
# |																								|
# | -> x₁ = x₂ -> [ 1, 1 ]																		|
# |																								|
# |    λ = 2 带入 ( A - λE ) x = 0 得到															|
# |    --   --   --  --   -- --																	|
# | -> | 3 3 | * | x₁ | = | 0 |																	|
# |    | 3 3 |   | x₂ |   | 0 |																	|
# |    --   --   --  --   -- --																	|
# |																								|
# | -> 3 * x₁ + 3 * x₂ = 0;																		|
# |    3 * x₁ + 3 * x₂ = 0;																		|
# |																								|
# | -> x₁ = -x₂ -> [ 1, -1 ]																	|
# |																								|
# | -> --   -- 结论:																				|
# |    | 5 3 | 特征值 λ = 8 或 λ = 2;																|
# |    | 3 5 | 作用在与向量 [ 1, 1 ] 平行方向上的向量, 则变成原向量的 8 倍								|
# |    --   -- 作用在与向量 [ 1, -1 ] 平行方向上的向量, 则变成原向量的 2 倍								|
# |---------------------------------------------------------------------------------------------|
