# x` = (AᵀA)⁻¹Aᵀb;
xbar = ( ( A.T * A ).I ) * A.T * b;
# xbar.shape: (2, 1)
# [ [ -3.89578088 ]
#   [  1.19303364 ] ]
print( "最优解: " );
print( xbar );
print( "函数为: y = {}x + {}".format( xbar[ 1, 0 ], xbar[ 0, 0 ] ) );

