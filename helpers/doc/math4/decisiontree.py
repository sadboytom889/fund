# |---------------------------------------------------------------------------------------------------------|
# | ID3 算法的缺陷																							|
# | 1: 无法处理连续性的特征, 因为 ID3 每次切分数据时选择的不是一个特征的取值, 而是一个具体的特征;							|
# | 该特征下有多少取值就会产生多少分叉, 若使用连续性特征比如西瓜的直径作为特征, 则理论上来说每个西瓜的直径都不同,				|
# | 这样的数据丢进 ID3 算法当中就会产生和样本数量相同的分叉, 这是没有意义的;											|
# |																											|
# | 2: 关于信息增益: 因为使用信息熵的差作为信息增益, 然后选择带来最大信息增益进行划分,									|
# | 会导致模型倾向于选择分叉比较多的特征, 极端情况下比如连续性特征, 此时条件熵:											|
# | h( y | x ) = ∑ⁿᵢ₌₁( p( x = xᵢ ) * ( h( x = xᵢ ) = 0 ) ) = 0, 则得到的信息增益非常大,						|
# | 但分叉多的特征并不一定划分效果好;																				|
# |---------------------------------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------------------------------|
# | C4.5 算法																								|
# | 相比于 ID3 采用信息增益作为分枝条件, C4.5 采用了信息增益率作为分枝条件;											|
# | 信息增益率: gainratio = g( y | xᵢ ) / H( xᵢ );															|
# | H( xᵢ ) = -( pᵢ * log₂( pᵢ ) ); 某种划分条件后的数据集对比总数据集得到的信息熵;									|
# | 比如: 共 10 个样本划分为 2 份和 8 份, 则分母为 (2/10) * log₂(2/10) + (8/10) * log₂(8/10)						|
# |																											|
# | 信息增益存在分母后若条件因子越多则 g( y | xᵢ ) 越大但对应的信息熵 H( xᵢ ) 也越大, 就能防止算法倾向选择因子过多的条件;	|
# |---------------------------------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------------------------------|
# | 对于第一个不能处理连续特征的问题, C4.5 将连续的特征离散化;														|
# | 比如 n 个样本的连续特征 A 有 n 个, 从小到大排列为:																|
# | a₁, a₂, ..., aₙ;																							|
# |																											|
# | 则 C4.5 取相邻两样本值的中位数, 共可取得 n - 1 个划分点, 其中第 i 个划分点表示 Tᵢ 表示为:							|
# | Tᵢ = ( aᵢ + aᵢ₊₁ ) / 2;																					|
# |																											|
# | 对于这 n - 1 个点: 分别计算以该点为分类点时其上下两部分的信息增益, 选择信息增益最大的点作为该连续特征的分类点:			|
# | 比如取到的增益最大的点为 aₜ, 则小于 aₜ 的值为类别 1, 大于 aₜ 的值为类别 2, 做到连续特征的离散化;						|
# |																											|
# | 对于连续值只切成两份, 因大多数情况每条数据连续值特征往往都不同, 没有办法很好地确定对于连续值特征究竟分成几个部分比较合理,	|
