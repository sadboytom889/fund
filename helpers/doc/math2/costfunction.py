# | 损失函数求导:																		|
# | j(θ) = ⁻¹⁄ₙ * ∑ⁿᵢ₌₁( yⁱ * log( p( xⁱ ) ) + ( 1 - yⁱ ) * log( 1 - p( xⁱ ) ) );	|
# |																					|
# | logistic:																		|
# | p(x) = 1 ⁄ ( 1 + e⁻ᶿᵀˣ );														|
# |																					|
# | 对数求导规则:																		|
# | 1: In( x )` = 1 ⁄ x;																|
# | 2: f`( g(x) ) = f`( t ) * g`( x ); 其中: t = g(x);								|
# | 3: In( f(x) )` = ( 1 ⁄ f(x) ) * f`( x );											|
# | 4: (e⁻ᶿˣ)` = e⁻ᶿˣ * (θ * x)` = e⁻ᶿˣ * -x;										|
# |																					|
# | 求导过程:																			|
# | 1: ∂⁄∂θⱼ( In( p(x) ) ) = ∂⁄∂θⱼ( In( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) )						|
# | 					   = ∂⁄∂θⱼ( In( 1 ) - In( 1 + e⁻ᶿᵀˣ ) )						|
# | 					   = ∂⁄∂θⱼ( -In( 1 + e⁻ᶿᵀˣ ) )								|
# | 					   = -( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) * ∂⁄∂θⱼ( 1 + e⁻ᶿᵀˣ )				|
# | 					   = -( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) * ∂⁄∂θⱼ( e⁻ᶿᵀˣ )					|
# | 					   = -( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) * e⁻ᶿᵀˣ * -x						|
# | 					   = ( 1 - ( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) ) * x						|
# |																					|
# | 2: ∂⁄∂θⱼ( In( 1 - p(x) ) ) = ∂⁄∂θⱼ( In( 1 - 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) )				|
# | 						   = ∂⁄∂θⱼ( In( e⁻ᶿᵀˣ ⁄ ( 1 + e⁻ᶿᵀˣ ) ) )				|
# | 						   = ∂⁄∂θⱼ( In( e⁻ᶿᵀˣ ) ) - ∂⁄∂θⱼ( In( 1 + e⁻ᶿᵀˣ ) )		|
# | 						   = ∂⁄∂θⱼ( -θᵀ * x ) - ∂⁄∂θⱼ( In( 1 + e⁻ᶿᵀˣ ) )			|
# | 						   = -x + ( 1 - ( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) ) * x				|
# | 						   = -( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) * x							|
# |																					|
# | 将 1, 2, p(x) 带入函数 j(θ) 求得关于 θⱼ 的导数:										|
# | ∂j(θ)⁄∂θ = ⁻¹⁄ₙ * ∑ⁿᵢ₌₁(															|
# | 	( yⁱ * x * ( 1 - ( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) ) ) -									|
# | 	( ( 1 - yⁱ ) * x * ( 1 ⁄ ( 1 + e⁻ᶿᵀˣ ) ) )									|
# | );																				|
# |																					|
# | ∂j(θ)⁄∂θ  = ⁻¹⁄ₙ * ∑ⁿᵢ₌₁(														|
# | 	( yⁱ * x * ( 1 - p(x) ) ) - ( ( 1 - yⁱ ) * x * p(x) )						|
# | );																				|
# |																					|
# | ∂j(θ)⁄∂θ  = ⁻¹⁄ₙ * ∑ⁿᵢ₌₁(														|
# | 	x * ( ( yⁱ * ( 1 - p(x) ) ) - ( ( 1 - yⁱ ) * p(x) ) )						|
# | );																				|
# |																					|
# | ∂j(θ)⁄∂θ  = ⁻¹⁄ₙ * ∑ⁿᵢ₌₁(														|
# | 	x * ( yⁱ - yⁱ * p(x) - p(x) + yⁱ * p(x) )									|
# | );																				|
# |																					|
# | ∂j(θ)⁄∂θ  = ¹⁄ₙ * ∑ⁿᵢ₌₁(	 x * ( p(x) - yⁱ ) );									|
# |																					|
# | 得到函数 j(θ) 求得关于 θⱼ 的导数:													|
# | ∂j(θ)⁄∂θ  = ¹⁄ₙ * ∑ⁿᵢ₌₁( x * ( p(x) - yⁱ ) );									|
# |																					|
# | 得到函数 j(θ) 求得关于 θ₀ 的导数:													|
# | ∂j(θ)⁄∂θ₀ = ¹⁄ₙ * ∑ⁿᵢ₌₁( p(x) - yⁱ );											|
