# | 1:																				|
# | if p( x ) >= 0.5: y` = 1;														|
# | if p( x ) < 0.5:  y` = 0;														|
# | # 若 p( x ) 小于 0.5 则认为 y` 为 0, 若 p( x ) 大于 0.5 则认为 y` 为 1;				|
# | # p( x ): sigmoid 函数得到的实际预测值												|
# | # y`: 预测结论值																	|
# |																					|
# | 2:																				|
# | if y == 1: p( x ) 越大 cost 越小, p( x ) 越小 cost 越大							|
# | if y == 0: p( x ) 越大 cost 越大, p( x ) 越小 cost 越小							|
# | # 若 p( x ) = 0.5 得到 y 为 1, 则 cost 最大										|
# | # 若 p( x ) = 1 得到 y 为 1, 则 cost 最小											|
# | #																				|
# | # 若 p( x ) = 0.499... 得到 y 为 0, 则 cost 最大									|
# | # 若 p( x ) = 0 得到 y 为 0, 则 cost 最小											|
# |																					|
# | 3:																				|
# | if y == 1: cost = -log( p( x ) );												|
# | if y == 0: cost = -log( 1 - p( x ) );											|
# | # 若 y 为 1 的损失函数图像															|
# | # log 函数 x 轴反转后取 0 < x < 1 部分的函数图像										|
# | # 过 ( 1, 0 ) 点的减函数, p( x ) = 0, cost = +∞; p( x ) = 1, cost = 0;				|
# | #																				|
# | # 若 y 为 0 的损失函数图像															|
# | # log 函数 x, y 轴反转向 x 轴正方向平移 1 后取 0 < x < 1 部分的函数图像					|
# | # 过 ( 0, 0 ) 点的增函数, p( x ) = 0, cost = 0; p( x ) = 1, cost = +∞;				|
# |																					|
# | 4:																				|
# | cost = -y * log( p( x ) ) - ( 1 - y ) * log( 1 - p( x ) );						|
# | # y 为 0 和 1 的情况合并后的单样本损失函数											|
