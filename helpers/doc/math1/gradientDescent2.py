# 通过梯度下降法寻找函数二元二次方程 f(x) 值最小时的 x 的值:

# 函数
def fn ( x, y ):
	return 0.6 * ( x + y ) ** 2 - x * y;

# 关于 x 的导函数
def dxfn ( x, y ):
	return 0.6 * 2 * ( x + y ) - y;

# 关于 y 的导函数
def dyfn ( x, y ):
	return 0.6 * 2 * ( x + y ) - x;

# 经过坐标点
gdpoint = [];

# 循环次数
count = 0;

# 关于 x 的导函数的初始 x 值
x = 10;

# 关于 y 的导函数的初始 x 值
y = 10;

# 初始 z 值
z = fn( x, y );

# 学习步长
step = 0.1;

# 添加初始坐标点
gdpoint.append( [ x, y, z ] );

while True:
	tempx, tempy = x, y;

	x = x - step * dxfn( tempx, tempy );

	y = y - step * dyfn( tempx, tempy );

	# 退出条件
	if z - fn( x, y ) < 1e-10:
		break;

	# 当前 y 值
	z = fn( x, y );

	# 记录坐标点
	gdpoint.append( [ x, y, z ] );

	count = count + 1;

gdpoint = np.array( gdpoint );

for point in gdpoint:
	print( "经过坐标点: {}".format( point ) );

print( "循环次数: {}".format( count ) );

print( "终点: {}".format( gdpoint[ count ] ) );

