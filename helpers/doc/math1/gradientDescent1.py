# | 2: 循环计算:																		|
# |	1): x₂ = x₁ - 0.1 * ( 8x + 5 ) = 0 - 0.1 * ( 8 * 0 + 5 ) = -0.5;				|
# | 	f`(0) = 5 > 0 -> x↑; y↑; -> x₁ - f`(x) * η;									|
# |																					|
# |	2): x₃ = x₂ - 0.1 * ( 8x + 5 ) = -0.5 - 0.1 * ( 8 * -0.5 + 5 ) = -0.6;			|
# | 	f`(-0.5) = 1 > 0 -> x↑; y↑; -> x₁ - f`(x) * η;								|
# |																					|
# |	3): x₄ = x₃ - 0.1 * ( 8x + 5 ) = -0.6 - 0.1 * ( 8 * -0.6 + 5 ) = -0.62;			|
# | 	f`(-0.6) = 0.2 > 0 -> x↑; y↑; -> x₁ - f`(x) * η;							|
# |																					|
# |	4): x₅ = x₄ - 0.1 * ( 8x + 5 ) = -0.62 - 0.1 * ( 8 * -0.62 + 5 ) = -0.624;		|
# | 	f`(-0.62) = 0.04 > 0 -> x↑; y↑; -> x₁ - f`(x) * η;							|
# |	... ...																			|
# |																					|
# |	循环中各项变化:																	|
# |	1): 结果值: -0.5 -> -0.6 -> -0.62 -> -0.624										|
# |	2): 导数值: 5 -> 1 -> 0.2 -> 0.04													|
# |	3): 调整值: 0.5 -> 0.1 -> 0.02 -> 0.004											|
# |																					|
# | 3: 收敛:																			|
# | xₙ ≈ min f(x);																	|
# |---------------------------------------------------------------------------------|

