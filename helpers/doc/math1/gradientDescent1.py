# 通过梯度下降法寻找函数一元二次方程 f(x) 值最小时的 x 的值:
# 步骤: 
# 1: 求梯度(微分);
# 2: 向梯度相反的方向移动 x: xᵢ₊₁ = xᵢ + η * f`( xᵢ ); η: 步长;
#    若步长太小则可保证每一次迭代都在减小, 但可能导致收敛太慢;
#    若步长太大则不能保证每一次迭代都减少, 也不能保证收敛;
# 3: 循环迭代步骤 1 ~ 2, 直到 x 的值变化到使得 f( x ) 在两次迭代之间的差值足够小(比如 0.00000001),
#    也就是说两次迭代计算出来的 f( x ) 基本没有变化, 则说明此时 f( x ) 已经达到局部最小值;
# 4: 输出 x, 这个 x 就是使得函数 f( x ) 最小时的 x 的取值;

# |---------------------------------------------------------------------------------|
# | 函数: f(x) = y = 4x² + 5x + 1;													|
# |---------------------------------------------------------------------------------|
# | 导函数: f`(x) = y = 8x + 5;														|
# |---------------------------------------------------------------------------------|
# | 梯度下降法: xᵢ₊₁ = xᵢ + η * f`( xᵢ );												|
# | xᵢ₊₁: 最新一次的结果;																|
# | xᵢ₊₁: 上一次的结果;																|
# | η: 步长;																			|
# | f`( xᵢ ): 导函数的值;																|
# |---------------------------------------------------------------------------------|

