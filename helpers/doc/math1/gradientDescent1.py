# |---------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------|
# |	导数绝对值越大, 每次针对结果的调整值越大, 向最小值逼近的步伐越大							|
# | 导数绝对值越小, 每次针对结果的调整值越小, 结果越趋近与最小值								|
# |---------------------------------------------------------------------------------|

# 函数
def fn ( x ):
	return 4 * ( x ** 2 ) + 5 * x + 1;

# 函数的导函数
def dfn ( x ):
	return 8 * x + 5;

# 经过坐标点
gdpoint = [];

# 循环次数
count = 0;

# 初始 x 值
x = -10;

# 初始 y 值
y = fn( x );

# 学习步长
step = 0.1;

# 添加初始坐标点
gdpoint.append( [ x, y ] );

while True:
	# 当前 x 值
	x = x - step * dfn( x );

	# 退出条件
	if y - fn( x ) < 1e-10:
		break;

	# 当前 y 值
	y = fn( x );

	# 记录坐标点
	gdpoint.append( [ x, y ] );

	count = count + 1;

gdpoint = np.array( gdpoint );

for point in gdpoint:
	print( "经过坐标点: {}".format( point ) );

print( "循环次数: {}".format( count ) );

print( "终点: {}".format( gdpoint[ count ] ) );

x = np.linspace( -10, 10, 100000 );

# 绘制函数
plt.plot( x, fn( x ), color = "r", linewidth = 0.8 );

# 绘制导函数
plt.plot( x, dfn( x ), color = "b", linewidth = 0.8 );

# 绘制途径坐标
plt.plot( gdpoint[ :, 0 ], gdpoint[ :, 1 ], "bo--", linewidth = 0.8 );

# 设置 x 轴的范围
plt.xlim( -10, 10 );

# 设置 y 轴的范围
plt.ylim( -10, 10 );

