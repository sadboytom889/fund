# |	当预测函数为 f( x ) = w * x + b; 是对损失函数求偏导									|
# |																					|
# | 预测函数: f( x ) = w * x + b														|
# | 损失函数: e( w, b ) = e` = ¹⁄₂ₙ * ∑ⁿᵢ₌₁( ( w * xⁱ + b - yⁱ )² );					|
# |																					|
# | 微分																				|
# | ∂e(w, b)⁄∂ = ¹⁄₂ₙ * ∑ⁿᵢ₌₁( ( ( f( xⁱ ) - yⁱ )² )` );								|
# | *): 导函数加法运算法制: ( f + g )` = f` + g`;										|
# |																					|
# | ∂e(w, b)⁄∂ = ¹⁄₂ₙ * ∑ⁿᵢ₌₁( ( ( f( xⁱ ) - yⁱ )² )` );								|
# | 		   = ¹⁄₂ₙ * ∑ⁿᵢ₌₁( 2 * ( ( f( xⁱ ) - yⁱ ) * ( f( xⁱ ) - yⁱ ) )` );		|
# | 		   = ¹⁄ₙ * ∑ⁿᵢ₌₁( ( f( xⁱ ) - yⁱ ) * ( f( xⁱ ) - yⁱ )` );				|
# | 		   = ¹⁄ₙ * ∑ⁿᵢ₌₁( ( w * xⁱ + b - yⁱ ) * ( w * xⁱ + b - yⁱ )` );			|
# | *): 复合函数求偏导: f`( g( x ) ) = f`( t ) * g`( x ); 其中: t = g( x );				|
# |																					|
# | 计算各项偏导数																		|
# | ∂e(w, b)/∂b = ¹⁄ₙ * ∑ⁿᵢ₌₁( ( f( xⁱ ) - yⁱ ) );									|
# | *): 对 ∂⁄∂b( w * xⁱ + b - yⁱ ) = 1; 代入后计算损失函数得到关于 b 的偏导数				|
# |																					|
# | ∂e(w, b)/∂w = ¹⁄ₙ * ∑ⁿᵢ₌₁( ( f( xⁱ ) - yⁱ ) * w );								|
# | *): 将 ∂⁄∂w( w * xⁱ + b - yⁱ ) = xⁱ; 代入后计算损失函数得到关于 w 的偏导数				|
