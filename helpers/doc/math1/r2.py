# |---------------------------------------------------------------------------------|
# | Total Sum of Squares:															|
# | TSS: 样本值均值与样本值的差距的平方只和												|
# | TSS = ( y¹ - yᵔ )² + ( y² - yᵔ )² + ( y³ - yᵔ )² + ... + ( yⁿ - yᵔ )²;			|
# | 	yⁱ: 样本值;																	|
# | 	yᵔ: 平均数 ( y¹ + y² + y³ + ... + yⁿ ) / n;									|
# |---------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------|
# | Residual Sum of Squares:														|
# | RSS: 样本值与预测值的差距的平方只和													|
# | RSS = ( y¹` - y )² + ( y²` - y )² + ( y³` - y )² + ... + ( yⁿ` - y )²;			|
# | 	y¹`: 样本值与预测值的差距; 														|
# |---------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------|
# | R²: 衡量模型预测值对于样本值的拟合程度, 比例越大, 模型越好;								|
# | R² = ( TSS + RSS ) / TSS;														|
# |---------------------------------------------------------------------------------|
