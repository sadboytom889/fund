# | 定积分: f( b ) - f( a ) = ∫ᵇₐ( df / dx ) * dx;									|
# | 代表: f( x ) 图形在 x 轴上方 a, b 之间的面积;										|
# | 比如运动中 y 坐标为速度 v, x 坐标为时间, 则运动的距离就是函数围成的图形面积				|
# |																					|
# | 例1:																				|
# | 若在坐标轴中放置 3 * 2 的长方形,														|
# | 长方形四个顶点坐标: ( 0, 2 ), ( 3, 2 ), ( 3, 0 ), ( 0, 0 );							|
# | 则:																				|
# | 导函数: df / dx = 2;																|
# | 原函数: f( x ) = 2x;																|
# | 定积分: ∫³₀( df / dx ) * dx = 2 * 3 - 2 * 0 = 6;									|
# | 结论: 定积分的结果代表图形面积;														|
# |																					|
# | 例2:																				|
# | 若在坐标轴中放置直角三角形, 三个顶点坐标: ( 0, 0 ), ( 2, 3 ), ( 2, 0 );				|
# | 则:																				|
# | 导函数: df / dx = 1.5x;															|
# | 原函数: f( x ) = 0.75 * x²;														|
# | 定积分: ∫²₀( df / dx ) * dx = 0.75 * 2² - 0.75 * 0² = 3.0;						|
# | 结论: 定积分的结果代表图形面积;														|
# |---------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------|
# | 函数平均数																		|
# | 定义为: f( x ) 中 x 在 a, b 之间的平均数为 ( 1 / ( b - a ) ) * ∫ᵇₐ( dy / dx ) * dx;	|
# | 意思是: 求得定积分也就是面积后除以 x 轴长度得到等分矩形的高为平均数;						|
# |																					|
# | 例:																				|
# | f( x ) = x²; 求得 0 < x < 2 之间的函数平均数等于多少;								|
# |																					|
# | u = ( 1 / ( b - a ) ) * ∫ᵇₐ( dy / dx ) * dx;									|
# |   = 1 / 2 * ∫²₀( x² ) * dx;														|
# |   = 1 / 2 * ( ( 1 / 3 ) * 2³ - ( 1 / 3 ) * 0³ );								|
# |   = 4 / 3;																		|
# |																					|
# | 则函数 f( x ) 在 0 到 2 之间的平均数是 4 / 3;										|
