# | 微分除法定律:																															|
# |																																		|
# | 推导:																																|
# | f( x ) = v( x ) / u( x );																											|
# |																																		|
# | f`( x ) = ( v( x ) * ( 1 / u( x ) ) )`;																								|
# | 		= v`( x ) * ( 1 / u( x ) ) + v( x ) * ( 1 / u( x ) )`;																		|
# | 		= v`( x ) * ( 1 / u( x ) ) + v( x ) * ( u`( x ) / u( x )² );																|
# | 		= ( v`( x ) * u( x ) + u`( x ) * v( x ) ) / u( x )²;																		|
# |																																		|
# | 结论:																																|
# | f`( x ) = ( v`( x ) * u( x ) + u`( x ) * v( x ) ) / u( x )²;																		|
# |-------------------------------------------------------------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------|
# | 符号系统:																			|
# |																					|
# | 牛顿符号:																			|
# | f`( x ) = y` = limₕ₌₀( ( f( x + h ) + f( x ) ) / h );							|
# |																					|
# | 莱布尼兹符号:																		|
# | dy / dx = df( x ) / dx = limₓ₌₀( Δy / Δx );										|
# | dx: Δx 变化量, 趋近于 0 的变化量;													|
# | dy: 随着 x 的变化量 Δx 的发生而导致的 y 的变化量;										|
# |---------------------------------------------------------------------------------|

# |---------------------------------------------------------------------------------|
# | 边际:																			|
# |																					|
# | dy / dx: x 的瞬间变化量导致 y 的变化量, 所以 dy / dx 代表着变化率;						|
# | dx * ( dy / dx ) = dy: x 的变化量 dx 的发生乘以变化率得到 dx 导致的 y 变化量 dy;		|
# | 边际: dy / dx 变化率在数学上叫做导数, 在金融学上叫做边际;								|
# | 	 比如: 边际成本指的是每一单位新增生产的产品带来的总成本的增量;						|
# |																					|
# | 例:																				|
# |																					|
# | 若:																				|
# | r = 收入; p = 获利; c = 成本; x = 产量;											|
# |																					|
# | 则:																				|
# | dp / dx: 代表边际获利: 因为 x 每一单位新增导致获利的增量;								|
# |																					|
# | 某手机销售 x 个, 获利函数为: p = 0.01x³ + 500x;										|
# |																					|
# | 计算销售 50 个手机时的边际获利:														|
# |																					|
# | 1: 计算导函数;																	|
# | dp / dx = 0.03x² + 500;															|
# |																					|
# | 2: 代入 x = 50 到 dp / dx;														|
# | 0.03 * 50 * 50 + 500 = 575;														|
# |																					|
# | 3: 结论																			|
# | 在销量为 50 个手机时, 每多生产一台手机获利 575 元; 也就是边际获利 575 元;					|
# |---------------------------------------------------------------------------------|
