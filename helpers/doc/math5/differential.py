# | 综合除法: f( x ) / g( x );														|
# | 对于多项式相除, 可使用综合除法;														|
# |																					|
# | 使用条件 1: g( x ) 满足一次多项式;													|
# | 使用条件 2: g( x ) 系数为 1;														|
# |																					|
# | 例1:	 f( x ) / g( x ) 综合除法的使用;												|
# | f( x ) = x³ - 3x² + 5x - 7;														|
# | g( x ) = x - 2;																	|
# |																					|
# | #######################################################							|
# | 步骤1: 记录系数;																	|
# | 1): 竖线左侧的数是被除数 f( x ) 的各项系数;											|
# | 2): 竖线右侧的数是 g( x ) 的常数项乘 -1;												|
# |																					|
# | 1,	3,	5,	-7  | 2																|
# | -----------------																|
# | #######################################################							|
# |																					|
# | #######################################################							|
# | 步骤2: 计算;																		|
# | 1): 首项直接挪下去																	|
# | 1,	-3,	5,	-7  | 2																|
# | 				|																|
# | -----------------																|
# | 1																				|
# | #############################													|
# |																					|
# | #############################													|
# | 2): 使用挪下去的首项乘以竖线右侧的数, 记录在第二项下方;									|
# | 1,	-3,	5,	-7  | 2																|
# | 	 2*1		|																|
# | -----------------																|
# | 1																				|
# | #############################													|
# |																					|
# | #############################													|
# | 3): 对齐的数相加并记录;																|
# | 1,	-3,	5,	-7  | 2																|
# | 	 2			|																|
# | -----------------																|
# | 1	-1																			|
# | #############################													|
# |																					|
# | #############################													|
# | 4): 重复;																		|
# | 1,	3,	5,	-7  | 2																|
# | 	2  -2	 6	|																|
# | -----------------																|
# | 1	-1	3 |	-1																	|
# | #############################													|
# |																					|
# | #######################################################							|
# | 步骤3: 得到商式系数为 1, -1, 3, 余式为 -1;											|
# | f( x ) / g( x ) = x² - x + 3 ... -1												|
# | q( x ) = x² - x + 3;															|
# | r( x ) = -1;																	|
# | #######################################################							|
# |																					|
# | #######################################################							|
# | 例2: 对于 g( x ) 某些条件不满足时, 可以设法转化后再使用综合除法;							|
# | f( x ) = 3x³ + 5x² - 8x + 4;													|
# | g( x ) = 3x - 1;																|
# | 此时 g( x ) 不满足系数为 1 的条件;													|
# |																					|
# | 但:																				|
# | f( x ) = ( 3x - 1 ) * q( x ) + r( x );											|
# | 	   = ( x - 1 / 3 ) * 3 * q( x ) + r( x );									|
# |																					|
# | 也就是若使用 ( x - 1 / 3 ) 作为被除数, 所得到的商式为原商式的 3 倍;						|
# |																					|
# | 则:																				|
# | 使用综合除法:																		|
# | 3	5	-8	+4	| 1 / 3															|
# |  	1	 2	-2	|																|
# | -----------------																|
# | 3	6	-6	| 2																	|
# |																					|
# | 得:																				|
# | q( x ) = ( 3x² + 6x - 6 ) / 3 = x² + 2x - 2;									|
# | r( x ) = 2;																		|
