# | 计算 Ax = 0 特解( 为自由变量分配特定值 0, 1; 因为求解 Ax = 0 方程的特解时, 若自由变量全部赋值为 0 得到所有分量为零, 也就是零向量 ):						|
# | 1): 取 x₂ = 1; x₄ = 0;																													|
# |     --  --                      --  --																									|
# |     | x₁ |  回代得到:	            | -2 | 该解代表:																							|
