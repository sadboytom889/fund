# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 一维向量的投影矩阵																															|
# | 例:																																		|
# | 若在二维空间中存在两个一维向量 a 与 b, 两向量之间的夹角为 θ, 向量 p 是 b 在 a 上的投影, 也称为 b 在 a 上的分量, 可用 b 乘以 a 方向的单位向量来计算			|
# | 投影过程中垂直于 a 并交与 b 的误差向量 e = b - p, 并且 a ⊥ e;																					|
# | 因 p 趴在 a 上, 故 p 实际上是 a 的一个子空间, 可将它看作 a 放缩 x 倍, 表示为 p = xa, 所以二者的点积为 0;											|
# |																																			|
# | 则:																																		|
# | a 向量与 e 正交:																															|
# | aᵀe = 0;																																|
# |																																			|
# | 将 p = xa, e = b - p 代入得到投影方程:																										|
# | aᵀ( b - xa ) = 0;																														|
# |																																			|
# | 化简得到:																																	|
# | aᵀb - xaᵀa = 0;																															|
# | xaᵀa = aᵀb;																																|
# |																																			|
# | 结果:																																	|
# | x = aᵀb / aᵀa;																															|
# | p = a( aᵀb / aᵀa );																														|
# |																																			|
# | 若将投影过程视为由某个矩阵完成, 称其为投影矩阵; 投影过程就是某个矩阵作用在某个向量上得到目标向量:														|
# | p = Pb;																																	|
# | 其中:																																	|
# | P: 投影矩阵;																																|
# | b: 被投影的向量;																															|
# |																																			|
# | 代入以上矩阵可得到 b 在 a 上的投影 p 向量:																									|
# | p = ( aaᵀ / aᵀa )b;																														|
# | 其中:																																	|
# | 公式表明当向量 b 放缩时, 向量 p 也放缩相同的倍数; a 放缩时, p 保持不变;																			|
# |																																			|
# | 得到投影矩阵:																																|
# | P = aaᵀ / aᵀa;																															|
# | 注意:																																	|
# | aᵀb 和 aᵀa 都是点积运算, 最后将得到一个标量数字, 向量是不能简单消去的;																			|
# | 其中:																																	|
# | 投影矩阵:																																	|
# | 向量 b 的在 a 上的投影 p 是一个矩阵 P 作用在 b 上得到的;																						|
# |																																			|
# | 投影矩阵分子 aᵀa:																															|
# | a 向量长度的平方, 是一个数字;																												|
# |																																			|
# | 投影矩阵分母 aaᵀ:																															|
# | a 是单列向量, aᵀ 是单行向量, 则投影矩阵 aaᵀ 是 2 * 2 矩阵; n 维空间中的单列 a 向量, 投影矩阵 aaᵀ 就是 n * n 方阵;									|
# |                            --                       --																					|
# |       --  --   --     --   | --  --      --  --      |																					|
# | aaᵀ = | x₁ | * | x₁ x₂ | = | | x₁ | * x₁ | x₁ | * x₂ |;																					|
# |       | x₂ |   --     --   | | x₂ |      | x₂ |      |																					|
# |       --  --               | --  --      --  --      |																					|
# |                            --                       --																					|
# | aᵀa 是一个标量, 所以 aaᵀ 决定了整个投影矩阵 P 的性质;																							|
# | 1: 因为 aaᵀ 的列向量来自于 a, aaᵀ 必然是线性相关的, a 的列空间一维的情况下其列空间和行空间的维度都是 1, 秩也是 1, rank( P ) = 1;						|
# | 2: 投影矩阵 P 乘以任意向量 b, 结果 Pb 总会停留在 P 的列空间 C( A ) 中, 在几何上 Pb 是趴在向量 a 上的投影向量 p, 所以 P 的列空间 C( A ) 也是趴在 a 上的;	|
# | 3: aaᵀ 也是方阵, 同时也是对称矩阵, Pᵀ = P;																									|
# | 4: P² 在几何上代表对 b 向量在 a 向量上投影两次 P², 将会得到投影一次的相同结果 P, 所以 P = P²;														|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 多维空间的投影矩阵																															|
# | 例:																																		|
# | A 矩阵列空间是二维平面, 列空间的基包含两个向量: a₁, a₂;																							|
# | 得到多维中 b 的投影 p 表达式: p = a₁x₁` + a₂x₂` = Ax`;	备注: x: 向量; A: 矩阵(多列矩阵);														|
# | 对比一维中 b 的投影 p 表达式: p = xa;					备注: x: 标量; a: 向量(单列矩阵);														|
# |																																			|
# | 误差向量 e = b - p 垂直于列空间平面意味垂直于平面上任意向量, 则它们和 e 的点积为 0, 则:																|
# | a₁e = a₁ᵀ(b – p) = 0;																													|
# | a₂e = a₂ᵀ(b – p) = 0;																													|
# |																																			|
# | 合并 a₁e 与 a₂e 得到:																														|
# | a₁ᵀ(b – p) + a₂ᵀ(b – p) = 0;																											|
# |																																			|
# | 转化为矩阵:																																|
# | --   --               --   --                 -- --																						|
# | | a₁ᵀ | * ( b - p ) = | a₁ᵀ | * ( b - Ax` ) = | 0 | = Aᵀ( b - Ax` ) = 0;																|
# | | a₂ᵀ |               | a₂ᵀ |                 | 0 |																						|
# | --   --               --   --                 -- --																						|
# |																																			|
# | 得到多维投影方程: Aᵀ( b - Ax` ) = 0;																										|
# | 对比一维投影方程: aᵀ( b - ax ) = 0;																										|
# | 且得到误差向量 e 属于 A 的基本子空间之左零空间 N( Aᵀ ); 因为列空间和左零空间正交, e 垂直于列空间所以 e 属于 A 的左零空间, 也就是 b - Ax` 属于 A 的左零空间;	|
# |																																			|
# | 计算投影过程中的其他量:																														|
# | Aᵀ( b - Ax` ) = 0;																														|
# | Aᵀb - AᵀAx` = 0;																														|
# | AᵀAx` = Aᵀb;																															|
# | (AᵀA)⁻¹AᵀAx` = (AᵀA)⁻¹Aᵀb;																												|
# | 1: x` = (AᵀA)⁻¹Aᵀb;				对比一维的情况: x = aᵀb / aᵀa;																				|
# | 2: p = Ax`-> p = A(AᵀA)⁻¹Aᵀb;	对比一维的情况: p = a( aᵀb / aᵀa );																		|
# | 3: p = Pb -> P = A(AᵀA)⁻¹Aᵀ;	对比一维的情况: P = aaᵀ / aᵀa;																				|
# |																																			|
# | 注意:																																	|
# | 1: 对比一维的情况得到: 多维的情况类似于一维, 但多维不存在矩阵除法, 故出现了逆矩阵;																	|
# | 2: 对于 P = A(AᵀA)⁻¹Aᵀ:																													|
# | 在 A 为 n * n 可逆方阵时可将结果化简为单位矩阵 I: P = A(AᵀA)⁻¹Aᵀ = AA⁻¹(Aᵀ)⁻¹Aᵀ = I;															|
# | 此时投影矩阵 P 列空间是整个 n 维空间, 投影的几何意义是将 b 投影到 n 维空间, 而 b 本身也属于 n 维空间(b 与投影矩阵有相同的行数), 所以 P = I;				|
# | 但大多数情况 A 不为方阵, 这时甚至不能将其化简为 AA⁻¹(Aᵀ)⁻¹Aᵀ, 因为非方阵的逆 A⁻¹ 不存在;															|
# |																																			|
# | 多维空间的投影矩阵满足和一维空间同样的性质:																										|
# | P² = P;	验证: P² = A(AᵀA)⁻¹AᵀA(AᵀA)⁻¹Aᵀ = A(AᵀA)⁻¹(AᵀA(AᵀA)⁻¹)Aᵀ = A(AᵀA)⁻¹Aᵀ;															|
# | Pᵀ = P; 验证: Pᵀ = (A(AᵀA)⁻¹Aᵀ)ᵀ = A(AᵀA)⁻¹Aᵀ;																							|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 投影的意义																																|
# | 若矩阵中: 行数(方程数) m 大于列数(未知数个数) n: m > n, 则此矩阵的秩 r 不可能大于 m( r < m ),														|
# | 当前 r < m 则是方程无解的情况, 仅在 b 存在于 A 的列空间中时存在解, 但 b 的取值在大部分情况下会导致方程组无解;											|
# |																																			|
# | 例1:																																		|
# |         --   --    --     --    --       --																								|
# |         | 1 1 |    | (1) 1 |    | (1)  0  |; 化简过程中第二步发现:	1 * x₂ = b₂;															|
# | 对于 A =	| 1 2 | -> |  0  1 | -> |  0  (1) |;                    4 * x₂ = b₃;															|
# |         | 1 5 |    |  0  4 |    |  0   0  |; m = 3, n = 2, r = 2; r < m;																|
# |         --   --    --     --    --       --																								|
# |             --   --   --  --   --  --																									|
# | 那么 Ax = b;	| 1 1 | * | x₁ | = | b₁ |; 不一定有解, 仅当 b 在 A 的列空间中时存在解;															|
# |             | 1 2 |   | x₂ |   | b₂ |																									|
# |             | 1 5 |   --  --   | b₃ |																									|
# |             --   --            --  --																									|
# | 若 b 取值没在 A 的列空间中, 可通过投影计算最接近可解的解;																						|
# | 换句话说: A 矩阵乘以 x 向量得到 Ax 总在列空间中, 而 b 不一定在列空间中, 故可将 b 调整为最接近列空间的分量 p;											|
# | 详细地说: Ax = b 需要将 b 微调成其在 A 的列空间上的投影 p;																						|
# | 其解 x: 在 Ax = b 转化为 Ax = p 过程中, 求解 Ax = p 的 x` 不是那个无解 Ax = b 的 x, 也就是 Ax` = p ≈ b;											|
# | 其中: 列空间平面上的向量有无数个, 其中最接近 b 的当然是 b 在列空间平面上的投影, b – p 产生最小的误差向量 e;											|
# | 特例: 若 b 本在列空间中, b 的投影 p 是其本身 b( b = p ), 若 b 正交于列空间, b 的投影是 0( p = 0 );												|
# |																																			|
# | 对于公式 p = A(AᵀA)⁻¹Aᵀb 存在的两种极端情况:																									|
# | 1: 假设向量 b 本身就在 A 矩阵列空间中;																										|
# | 几何上: 投影的结果应是 b 本身: p = Pb = b;																									|
# | 公式上: b 本身在列空间中, 因列空间中任意向量可表示为 Ax (x 为向量, Ax 为 A 列空间中任意向量), 即 b = Ax: p = A(AᵀA)⁻¹AᵀAx = A(AᵀA)⁻¹(AᵀA)x = Ax = b;	|
# |																																			|
# | 2: 假设向量 b 垂直于 A 矩阵列空间;																											|
# | 几何上: 投影的结果应是零向量: p = Pb = 0;																									|
# | 公式上: b 垂直于 A 矩阵列空间, 则 b 属于 A 的左零空间得到 Aᵀb = 0: p = A(AᵀA)⁻¹(Aᵀb) = 0;														|
# |																																			|
# | 例2:																																		|
# | 除了对损失函数使用梯度下降法求得线性回归, 还可使用投影的思想: 最小二乘法																			|
# | 假设平面中存在 (1, 1), (2, 2), (3, 2) 三个数据点, 需要计算不会穿过所有点并其使总误差最小的直线: y = ax + b;											|
# |																																			|
# | 若使用方程组则表示为:																														|
# | 1c + d = 1;																																|
# | 2c + d = 2;																																|
# | 3c + d = 2;																																|
# |																																			|
# | 使用矩阵 Ax = b 表示:																														|
# | --   --   -- --   -- --																													|
# | | 1 1 | * | c | = | 1 |;																												|
# | | 2 1 |   | d |   | 2 |																													|
# | | 3 1 |   -- --   | 2 |																													|
# | --   --           -- --																													|
# |                         -- --  -- --                                                            -- --									|
# |                         | 1 |  | 1 |                                                            | 1 |									|
# | A 矩阵列空间是: C( A ) = (	| 2 |, | 1 |), r( A ) = 2, r < m 方程无解, 除非 b 存在于 A 的列空间中, 然而 b	| 2 | 没存在于 A 的列空间中;				|
# |                         | 3 |  | 1 |                                                            | 2 |									|
# |                         -- --  -- --                                                            -- --									|
# |                          -- --																											|
# | 所以需要投影寻找最优解:	x` = | c |;																											|
# |                          | d |																											|
# |                          -- --																											|
# |																																			|
# | 使用投影公式: AᵀAx` = Aᵀb 计算最优解:																										|
# | --     --   --   --   -- --   --     --   -- --																							|
# | | 1 2 3 | * | 1 1 | * | c | = | 1 2 3 | * | 1 |;																						|
# | | 1 1 1 |   | 2 1 |   | d |   | 1 1 1 |   | 2 |																							|
# | --     --   | 3 1 |   -- --   --     --   | 2 |																							|
# |             --   --                       -- --																							|
# | --    --   -- --   --  --																												|
# | | 14 6 | * | c | = | 11 |;																												|
# | |  6 3 |   | d |   |  5 |																												|
# | --    --   -- --   --  --																												|
# |																																			|
# | 得到方程:							解方程:  					 --   --																		|
# | 14c + 6d = 11;					c = 1/2;			x` = | 1/2 |;																		|
# | 6c + 3d = 5;					d = 2/3;				 | 2/3 |																		|
# |                     			        				 --   --																		|
# | 最优直线为:																																|
# | y = (1/2)x + (2/3);																														|
# |																																			|
# | 计算 p = Ax`:																															|
# | --   --   --   --   --    --            --    --																						|
# | | 1 1 | * | 1/2 | = |  7/6 |;  得到:	p = |  7/6 |;																						|
# | | 2 1 |   | 2/3 |   |  5/3 |            |  5/3 |																						|
# | | 3 1 |   --   --   | 13/6 |            | 13/6 |																						|
# | --   --             --    --            --    --																						|
# |																																			|
# | 计算 e = b - p:																															|
# |     -- --   --    --   --    --   --    --   --    --																					|
# |     | 1 |   |  7/6 |   |  6/6 |   |  7/6 |   | -1/6 |																					|
# | e = | 2 | - |  5/3 | = | 12/6 | - | 10/6 | = |  1/3 |;																					|
# |     | 2 |   | 13/6 |   | 12/6 |   | 13/6 |   | -1/6 |																					|
# |     -- --   --    --   --    --   --    --   --    --																					|
# |																																			|
# | p 与 e 存在垂直关系 p ⊥ e:																													|
# |                                     --               --   --    --																		|
# | 则 p 与 e 的点积 pe = 0 -> pᵀe = 0:	| 7/6  10/6  13/6 | * | -1/6 | = 0;																	|
# |                                     --               --   |  2/6 |																		|
# |                                                           | -1/6 |																		|
# |                                                           --    --																		|
# | A 上的每个向量与 e 都存在垂直关系:																											|
# |                 -- --    --       --   --    --																							|
# |                 | 1 |    | 1  2  3 | * | -1/6 | = 0;																					|
# | 比如 A 的一个基:	| 2 |,   --       --   |  2/6 |																							|
# |                 | 3 |                  | -1/6 |																							|
# |                 -- --                  --    --																							|
# |																																			|
# | 对损失函数使用梯度下降法求得线性回归:																											|
# | 使用误差平方和作为测量误差的标准, 误差: e = b - p = ||Ax` - p||²;																				|
# |																																			|
# | 损失函数:	e = ∑ⁿᵢ₌₁( eᵢ² );																												|
# |           = ( 1 - ( 1c + d ) )² + ( 2 - ( 2c + d ) )² + ( 2 - ( 3c + d ) )²;															|
# |           = 14c² - 22c - 10d + 12cd + 3d² + 9;																							|
# |																																			|
# | 对损失函数求偏导得到方程:			解方程:																									|
# | ∂e⁄∂c = 28c + 12d - 22 = 0;		c = 1/2;																								|
# | ∂e⁄∂d = 12c + 6d - 10 = 0;		d = 2/3;																								|
# |																																			|
# | 最优直线为:																																|
# | y = (1/2)x + (2/3);																														|
