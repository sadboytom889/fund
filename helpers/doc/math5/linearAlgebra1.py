# |     --  --                      --  --																									|
# |         --  --																															|
# |         | -2 |																															|
# | x = c * |  1 | 该解为零空间( R⁴ 空间中的一条直线 ) Ax = 0 的解集中的一个向量, x 取任意倍数均满足;													|
# |         |  0 |																															|
