# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 零空间																																	|
# | 定义: 矩阵 A ( m * n, 列向量 x ) 的零空间记作: N( A ) 指满足 N( A ) = { x ∈ Rⁿ | Ax = 0 }; 的所有解的集合;										|
# |																																			|
# | 例:																																		|
# |             --     --																													|
# |             | 1 1 2 |																													|
# | 对于矩阵 A:	| 2 1 3 |																													|
# |             | 3 1 4 |																													|
# |             | 4 1 5 |																													|
# |             --     --																													|
# | 分析:																																	|
# | A 的零空间反应的是方程 Ax = 0 的解集, 也就是解集所张成的空间, "零" 的含义不是解只有一个零向量, 而是线性方程组等于零向量, 即 b = 0;						|
# | x 是包含 3 个分量的向量, 故矩阵 A 的零空间是 R³ 的子空间; 而子空间也包括零向量, 即: x 等于零向量;													|
# | 对于 m * n 矩阵, 列空间为 Rᵐ 的子空间，零空间为 Rⁿ 空间的子空间;																				|
# |																																			|
# | A 的零空间:																																|
# |             --     --   --  --   -- --																									|
# |             | 1 1 2 |   | x₁ |   | 0 |																									|
# | 由 Ax = 0:	| 2 1 3 | * | x₂ | = | 0 | 可以得到:																							|
# |             | 3 1 4 |   | x₃ |   | 0 |																									|
# |             | 4 1 5 |   --  --   -- --																									|
# |             --     --																													|
# |     --  --                                                                      --  --      -- --										|
# |     |  1 |                                                                      |  c |      | 0 |										|
# | 包含	|  1 | 的任何倍数的集合, 因为看到第一列向量和第二列向量相加减去第三列向量为零, 可以表示为	|  c |, 包含	| 0 | 当 c 取 0 时;							|
# |     | -1 |                                                                      | -c |      | 0 |										|
# |     --  --                                                                      --  --      -- --										|
# | 则 A 的零空间为 R³ 中的一条直线																												|
# |																																			|
# | 推导 Ax = 0 的解集( 零空间 )是一个向量空间:																									|
# |																																			|
# | 假设:																																	|
# | v 和 w 为解集中的元素																														|
# |																																			|
# | 则:																																		|
# | Av + Aw = A( v + w ) = 0 + 0 = 0; => 推导得到满足加法封闭性																					|
# | cAv = A( c * v ) = 0; => 推导得到满足乘法封闭性																								|
# |																																			|
# | 结论:																																	|
# | A 的零空间 N( A ) 确实向量空间																												|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 计算零空间																																|
# | 例1:																																		|
# |																																			|
# | 线性方程组:																																|
# | 1x₁ + 2x₂ + 2x₃ +  2x₄ = 0;																												|
# | 2x₁ + 4x₂ + 6x₃ +  8x₄ = 0;																												|
# | 3x₁ + 6x₂ + 8x₃ + 10x₄ = 0;																												|
# |																																			|
# | 转化为矩阵:																																|
# |     --          --																														|
# |     | 1  2  2  2 |																														|
