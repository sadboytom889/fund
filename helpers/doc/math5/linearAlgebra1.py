# |     | 0  0  0  0 |																														|
# |     --          --																														|
# | U 第三行全是 0 代表着第三行是第一行和第二行的线性组合;																							|
# |																																			|
# | 计算秩:																																	|
# |              |->自由列<-|																													|
# |              |         |																												|
# |     --       |         | --  包含两个主列( 第一列和第三列 )和两个自由列( 第二列和第四列 ), 且秩 R( A ) = 2;										|
# |     | ((1)) [2] ( 2 ) [2] |  主列: 主元所在的列;		主变量: 处在主列的分量;																	|
# | U =	| ( 0 ) [0] ((2)) [4] |  自由列: 主元不在的列;		自由变量: 没有处在主列的分量;															|
