# |     | 验证:																																|
# |     | |  0  0 | = 0 * b₂ - 0 * a₂ = 0; | a₁ 0 | = 0 * a₁ - 0 * a₂ = 0;																	|
# |     | | a₂ b₂ |                        | a₂ 0 |																							|
# |     |																																	|
# |     | 结论:																																|
# |     | | 0   0 | = | a₁ 0 | = 0; 即: 某行或列的元素全为 0, 则 Det(A) = 0;																	|
# |     | | a₂ b₂ |   | a₂ 0 |																												|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |																																			|
# | 8: 任何矩阵 A 通过消元法被转化为上三角矩阵 U, U 的行列式为对角线上的项(主元)相乘: d₁ * d₂ * d₃ * ... * dₙ;											|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |     | 推导:																																|
# |     |     --                   --                                           --                 --										|
# |     |     |  d₁   x   x ...  x  |                                           | d₁   x   x ...  x |										|
# |     |     |   x  d₂   x ...  x  |                                           |  0  d₂   x ...  x |										|
# |     | A = |   x   x  d₃ ...  x	|; 根据性质 6 消元不影响行列式的值将其转化为: U =	|  0   0  d₃ ...  x |;										|
# |     |     |   ...               |                                           |  ...              |										|
# |     |     |   x   x   x ...  dₙ	|                                           |  0   0   0 ... dₙ	|										|
# |     |     --                   --                                           --                 --										|
# |     |                                                       --                 --														|
# |     |                                                       | d₁   0   0 ...  0 |														|
# |     |                                                       |  0  d₂   0 ...  0 |														|
# |     |                            x 可继续通过消元法完全消除得到:	|  0   0  d₃ ...  0 |;														|
# |     |                                                       |  ...              |														|
# |     |                                                       |  0   0   0 ... dₙ	|														|
# |     |                                                       --                 --														|
# |     |                                                                                --             --									|
# |     |                                                                                |  1  0 0 ... 0 |									|
# |     |                                                                                |  0  1 0 ... 0 |									|
# |     |                            根据性质 3 (倍乘性质) 转化为: d₁ * d₂ * d₃ * ... * dₙ *	 |  0  0 1 ... 0 |;									|
# |     |                                                                                |  ...          |									|
# |     |                                                                                |  0  0 0 ... 1 |									|
# |     |                                                                                --             --									|
# |     |																																	|
# |     |                            根据性质 1 转化为:  d₁ * d₂ * d₃ * ... * dₙ * | I |  = d₁ * d₂ * d₃ * ... * dₙ;							|
# |     |																																	|
# |     | 得到:																																|
# |     | 1: 任意可逆矩阵的行列式是各主元的乘积;																									|
# |     | 2: 注意: 行列式的符号需要判断在 A 转化为 U 时, 经过了多少次的行变换, 根据性质 2: |A| = -|A|;													|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |																																			|
# | 9: 矩阵 A 行列式 Det(A) = 0 代表矩阵是奇异矩阵, 矩阵 A 行列式 Det(A) ≠ 0 代表矩阵是可逆矩阵;														|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |     | 推导:																																|
# |     | 由性质 8 可得到结论;																													|
# |     | 1: 在消元过程中若遇到主元为 0 且无处可转时, 得到一个全零行, 也就是矩阵为奇异矩阵同时根据性质 7, 得到矩阵的行列式为 0;								|
# |     | 2: 在消元过程中若每行都存在主元, 也就是矩阵是可逆矩阵, 同时得到矩阵的行列式的值就是各主元的乘积;													|
# |     |																																	|
# |     | 换句话说, 行列式检验矩阵是否可逆性:																										|
# |     | 1: 行列式非零代表矩阵可逆;																											|
# |     | 2: 行列式为零代表矩阵奇异;																											|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |																																			|
# | 10: 两个矩阵相乘的行列式, 等于两个矩阵的行列式相乘 Det( A * B ) = Det( A ) * Det( B );															|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |     | 推导:																																|
# |     | 因为可逆矩阵 A 都可消元化简成最简形式对角阵, 则:																							|
# |     | Det( A ) = d₁ * d₂ * d₃ * ... * dₙ;																								|
# |     |																																	|
# |     | 因为对角矩阵 A 的逆矩阵 A⁻¹ 也是对角矩阵, 对角线上的项是原矩阵的项的倒数, 则:																|
# |     | Det(A⁻¹) = (1 / d₁) * (1 / d₂) * (1 / d₃) * ... * (1 / dₙ);																		|
# |     |																																	|
# |     | 得到:																																|
# |     | Det( A ) * Det(A⁻¹) = d₁ * d₂ * d₃ * ... * dₙ * (1 / d₁) * (1 / d₂) * (1 / d₃) * ... * (1 / dₙ) = I;								|
# |     |																																	|
# |     | 注意: Det(A²) = Det(A)²;																											|
# |     | 但是: Det(2A) ≠ 2 * Det(A); 因为 2 * Det(A) 代表只对 A 的某行进行倍乘, 而 Det(2A) 代表每一行都进行倍乘;									|
# |     | 所以: Det(2A) = 2ⁿ * Det(A); n 代表行数, 就像是体积一样, 若每条边都乘以 2, 体积增大 2ⁿ 倍;													|
# |     |																																	|
# |     | 验证:																																|
# |     | 因为:																																|
# |     | A⁻¹A = I;																															|
# |     |																																	|
# |     | 根据性质 1:																															|
# |     | Det(A⁻¹A) = Det(I) = 1;																											|
# |     |																																	|
# |     | 同时:																																|
# |     | Det(A⁻¹) = 1 / Det(A);																											|
# |     |																																	|
# |     | 结论:																																|
# |     | Det(A⁻¹A) = Det(A⁻¹) * Det(A);																									|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |																																			|
# | 11: 矩阵 A 的行列式等于 Aᵀ 的行列式, 即 Det(A) = Det(Aᵀ);																					|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |     | 推导:																																|
# |     | 矩阵 A 可转化为 L * U:																												|
# |     | A = L * U;																														|
# |     |																																	|
# |     | 矩阵 A 的转置可转化为 Uᵀ * Lᵀ:																										|
# |     | Aᵀ = Uᵀ * Lᵀ;																														|
# |     |																																	|
# |     | 则目前需要证明 |Uᵀ * Lᵀ| = |L * U| 以证明 Det(A) = Det(Aᵀ);																			|
# |     |																																	|
# |     | 根据性质 10:																														|
# |     | |Uᵀ| * |Lᵀ| = |L| * |U|;																											|
# |     |																																	|
# |     | 其中: L(L = E⁻¹) 是下三角矩阵, 对角线上全是 1, 对角线上全是 0, 则:																		|
# |     | |L| = 1; |Lᵀ| = 1;																												|
# |     |																																	|
# |     | 代入 |L| = 1; |Lᵀ| = 1 得到:																										|
# |     | |Uᵀ| = |U|;																														|
# |     |																																	|
# |     | 其中: U 代表上三角矩阵, U 的行列式等于对角线上的主元相乘, Uᵀ 对角线项不变, 则证明:															|
# |     | |Uᵀ| = |U|;																														|
# |     |																																	|
# |     | 得到:																																|
# |     | Det(A) = Det(Aᵀ);																													|
# |     |																																	|
# |     | 验证:																																|
# |     | | a₁ b₁ | = a₁ * b₂ - b₁ * a₂ = a₁ * b₂ - a₂ * b₁ = | a₁ a₂ |;																	|
# |     | | a₂ b₂ |                                           | b₁ b₂ |																		|
# |     |																																	|
# |     | 结论:																																|
# |     | Det(A) = Det(Aᵀ);																													|
# |     -----------------------------------------------------------------------------------------------------------------------------		|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 行列式的意义																																|
# | 当 Det( A ) ≠ 0 时, 意味着组成 |A| 的向量全部线性无关, 即向量围成的 n 维空间中图形的 n 维体积不为 0, 线性方程组有唯一解;								|
# | 当 Det( A ) = 0 时, 意味着至少有一个向量是"多余"的, 正是这个多余的向量使得 n 维图形的体积为 0, 方程组无解或有无数解;									|
# |																																			|
# | 线性代数研究的是向量之间的关系, 向量间最重要的关系就是独立或不独立, 行列式是否等于 0 正是这种关系的有效描述;												|
# |																																			|
# | 例:																																		|
# | | 1 2 | ≠ 0 -> 1x₁ + 2x₂ = 0; 方程存在唯一解;																								|
# | | 3 4 |        3x₁ + 4x₂ = 0;																											|
# |																																			|
# | | 1 2 | ≠ 0 -> 1x₁ + 2x₂ = 0; 方程存在无数解;																								|
# | | 2 4 |        2x₁ + 4x₂ = 0;																											|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

