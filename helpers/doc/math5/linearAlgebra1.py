# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 向量内积( 点乘, 点积, 数量积 )																												|
# | 几何公式:																																	|
# | 向量 b 在 a 上的投影的模长再乘以 a 的模长: a·b = ||a|| * ||b|| * cosθ;																		|
# |																																			|
# | 推导:																																	|
# | a, b 向量线性无关且形成 θ 夹角, a - b 为 a 向量终点连接到 b 向量终点记作 c;																		|
# | 得到:																																	|
# | c = a - b;																																|
# |																																			|
# | 三角形余弦定理:																															|
# | c² = a² + b² - 2 * ||a|| * ||b|| * cosθ;																								|
# |																																			|
# | 代入 c = a - b 三角形余弦定理:																												|
# | (a - b)² = a² - 2a·b + b²;																												|
# |          = a² + b² - 2 * a·b;																											|
# |          = a² + b² - 2 * ||a|| * ||b|| * cosθ;																							|
# |																																			|
# | 得到:																																	|
# | 点积是两个向量的长度与它们夹角余弦的积: a·b = ||a|| * ||b|| * cosθ;																			|
# |																																			|
# | 坐标公式:																																	|
# | a·b = a₁ * b₁ + a₂ * b₂;																												|
# |																																			|
# | 推导:																																	|
# | 设定 a 的与坐标轴的 θ₂, b 的与坐标轴的 θ₁ 且 a 与 b 的夹角: θ = θ₂ - θ₁																		|
# | 可使用两角差的三角函数公式: cos(θ₂ - θ₁) ＝ cosθ₁ * cosθ₂ + sinθ₁ * sinθ₂;																	|
# | a·b = ||a|| * ||b|| * cos(θ₂ - θ₁);																										|
# |     = √(a₁² + a₂²) * √(b₁² + b₂²) * (cosθ₁ * cosθ₂ + sinθ₁ * sinθ₂);																	|
