# | Ax = b 的通解																																|
# |																																			|
# | 通解推导:																																	|
# |																																			|
# | 假设:																																	|
# | Ax = 0 解集零空间的任意向量是 xₙ, Ax = b 有一个特解 xₚ, 则																						|
# |																																			|
# | 已知:																																	|
# | Axₚ = b;																																	|
# | Axₙ = 0;																																	|
# |																																			|
# | 相加:																																	|
# | Axₚ + Axₙ = b;																															|
# | A( xₚ + xₙ ) = b;																														|
# |																																			|
# | 结论:																																	|
# | 通解: xₙ + xₚ 成为方程组的通解, 对于方程组的某解 xₚ 来说, xₚ 与零空间内任意向量之和仍为解;															|
# |																																			|
# | 消元法化简:																																|
# | --           --   --  --    --           --   --        --																				|
# | | 1  2  2   2 |   | b₁ |    | (1) 2  2  2 |   |       b₁ |																				|
# | | 2  4  6   8 | * | b₂ | -> |  0  0  2  4 | * | b₂ - 2b₁ |																				|
# | | 3  6  8  10 |   | b₃ |    |  0  0  2  4 |   | b₃ - 3b₁ |																				|
# | --           --   --  --    --           --   --        --																				|
# |                             --           --   --            --																			|
# |                             | (1) 2  2  2 |   |           b₁ |																			|
# |                          -> |  0  0 (2) 4 | * |     b₂ - 2b₁ |																			|
# |                             |  0  0  0  0 |   | b₃ - b₂ - b₁ |( b₃ - 3b₁ - ( b₂ - 2b₁ ) = b₃ - 3b₁ - b₂ + 2b₁ = b₃ - b₂ - b₁ )			|
# |                             --           --   --            --																			|
# |																																			|
# | 结论:																																	|
# | 1: 对于左侧矩阵某行全是 0 可得出右侧矩阵某行也为 0( 0x₁ + 0x₂ + 0x₃ + ... + 0xₙ = 0 );															|
# |                                                                     -- --  -- --														|
# |                                                                     | 1 |  | 2 |														|
# | 2: 仅当 b 属于 A 的列空间时有解( 使用列空间的基描述列空间 C( A ) = span(	| 2 |, | 6 |   ) );													|
# |                                                                     | 3 |  | 8 |														|
