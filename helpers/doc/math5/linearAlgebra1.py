# | 向量外积( 叉乘, 叉积, 向量积 )																												|
# | 定义:																																	|
# | 两个向量 a, b 的外积是一个向量 c 且 a, b 的外积 c 与 a, b 两个向量组成的坐标平面垂直且 c 模长等于 a, b 两个向量为边的平行四边形的面积;					|
# | c 的方向由右手定则决定: 右手食指指向 a 方向, 中指指向 b 方向, 拇指的方向即为 c 的方向:																|
# | a * b = c;																																|
# | b * a = -c;																																|
# |																																			|
# | 根据定义 a, b 的夹角 θ 若为 0, 即 a, b 平行:																									|
# | a * b = 0;																																|
# |																																			|
# | 几何公式:																																	|
# | 外积的绝对值表示两个向量张成的平行四边形的面积: a * b = |a| * |b| * sinθ;																		|
# | 推导:																																	|
# | |a| * sinθ 等于 以 b 为底平行四边形的高 -> |b| * |a| * sinθ = 平行四边形的面积;																|
# | |b| * sinθ 等于 以 a 为底平行四边形的高 -> |a| * |b| * sinθ = 平行四边形的面积;																|
