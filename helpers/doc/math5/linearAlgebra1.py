# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 方程组的几何意义:																															|
# |																																			|
# | 例:																																		|
# | 包含两个未知数的两个方程组:																													|
# | f1( x ) = 2x - y = 0;																													|
# | f2( x ) = -x + 2y = 3;																													|
# |																																			|
# | 函数图像: 对函数 f1( x ) 与 f2( x ) 在坐标系中作图, 其解将是两条线的交点, 因为交点坐标能同时满足两个方程;												|
# |																																			|
# | 矩阵: 系数矩阵: A	*	未知数向量: X	=	结果向量: b																							|
# | 	--	   --	 	--     --	 	--     --																							|
# | 	|  2 -1 |	*	|   x   |	=	|   0   |																							|
# | 	| -1  2 |	 	|   y   |	 	|   3   |																							|
# | 	--	   --	 	--     --	 	--     --																							|
# |																																			|
# | 	公式: A * X = b;	其中: A * X 可将其理解为 A 各列的线性组合, 而线性组合的解为	--  --															|
# | 		--	     --   --  --   --  --									| x₁ |															|
# | 		| A₁₁ A₁₂ | * | x₁ | = | b₁ |									| x₂ |															|
# | 		| A₂₁ A₂₂ |   | x₂ |   | b₂ |									--  --															|
# | 		--	     --   --  --   --  --																									|
# |																																			|
# | 			--	 --		   --	--																										|
# | 	= x₁ *	| A₁₁ | + x₂ * | A₁₂ |																										|
# | 			| A₂₁ |		   | A₂₂ |																										|
# | 			--	 --		   --	--																										|
# |																																			|
# | 	   --                   --																											|
# | 	=  | x₁ * A₁₁ + x₂ * A₁₂ |																											|
# | 	   | x₁ * A₂₁ + x₂ * A₂₂ |																											|
# | 	   --                   --																											|
# |																																			|
# | 线性组合:	    --   --       --   --   --   --                    	--   --   	--   --             --   --									|
# | 		x * |  2  | + y * | -1  | = |  0  | : 方程的目的在于如何将	|  2  |	与	| -1  | 正确的组合构成	|  0  |									|
# | 		    | -1  |       |  2  |   |  3  |                    	| -1  |   	|  2  |             |  3  |									|
# | 		    --   --       --   --   --   --                    	--   --   	--   --             --   --									|
# | 		x = 1, y = 2 代表: 1 倍长度的	--   -- 加上 2 倍长度的 --   -- 将得到目标向量	--   --;												|
# | 									|  2  |				  | -1  |				|  0  |													|
# | 									| -1  |				  |  2  |				|  3  |													|
# | 									--   --				  --   --				--   --													|
# |																																			|
# | 列图像: 对向量	--   --  与	--   -- 在坐标系中作图, 其解将是如何构造两向量使结果满足目标向量	--   --;												|
# | 			|  2  |		| -1  |													|  0  |													|
# | 			| -1  |		|  2  |													|  3  |													|
# | 			--   --		--   --													--   --													|
# |																																			|
# | 思考得到: 1: 以上两方程所有的线性组合将铺满整个平面, 但并不是所有的两个未知数两个方程的线性组合都能铺满整个平面;											|
# | 			比如两个向量平行的情况;																											|
# |																																			|
# | 		 2: 同理三个未知数三个方程的列线性组合也不一定覆盖整个三维空间;																			|
# | 		    比如三个列向量正好处在同一平面, 不论如何组合也不会出现超过该平面的 3 维的情况;															|
