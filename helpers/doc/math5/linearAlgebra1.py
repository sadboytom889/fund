# | 正交向量																																	|
# | 理解:																																	|
# | 正交是垂直的另一说法, 两个向量正交意味着两个向量的夹角是 90°; Rⁿ 空间内的向量 x 和向量 y 正交时, 二者的点积是 0;										|
# |																																			|
# | 公式:																																	|
# | 当 x, y 正交时两者点积 xy = xᵀy = 0;																										|
# |																																			|
# | 推导:																																	|
# | 直角三角形中:																																|
# | ||x||² + ||y||² = ||x + y||²; ( ||x|| 和 ||y||: 直角三角形的直角边向量模长, ||x + y||: 直角三角形的斜边模长 );									|
# |																																			|
# | 其中:																																	|
# | ||x||² = xᵀx; ||y||² = yᵀy; ||x + y||² = (x + y)ᵀ(x + y);																				|
# |       --     --																															|
# |       |  x₁	  |																															|
# |       |  x₂	  |   --                    --																								|
# | xᵀx = |  x₃	  | * | x₁   x₂   x₃ ...  xₙ | = xᵀx = x₁² + x₂² + x₃² + ... + xₙ² = ||x||²;													|
# |       |  ...  |   --                    --																								|
# |       |  xₙ	  |																															|
# |       --     --																															|
# |																																			|
# | 代回直角三角形公式:																															|
# | xᵀx + yᵀy = (x + y)ᵀ(x + y);																											|
# | xᵀx + yᵀy = xᵀx + xᵀy + yᵀx + yᵀy;																										|
# | 0 = xᵀy + yᵀx;( xᵀy 与 yᵀx 实际上是相同结果 );																								|
# | 0 = 2xᵀy;																																|
# |																																			|
# | 最终得到:																																	|
# | xᵀy = 0;																																|
# | xᵀy 正是公式 ||x||² + ||y||² = ||x + y||² 成立的条件: 向量 x, y 正交( 构成直角三角形 );														|
# |																																			|
# | 注意:																																	|
# | 当向量 x 或 y 其中一个是零向量, 它们也正交; 零向量和任意向量正交, 因为它满足规则 xᵀy = 0;															|
# |																																			|
# | 例:																																		|
# |     -- --																																|
# |     | 1 |																																|
# | x = | 2 |; ||x||² = 1² + 2² + 3² = 14;																									|
# |     | 3 |																																|
# |     -- --																																|
# |     --  --																																|
# |     |  2 |																																|
# | y = | -1 |; ||y||² = 2² + -1² + 0² = 5;																									|
# |     |  0 |																																|
# |     --  --																																|
# |         -- --																															|
# |         | 3 |																															|
# | x + y = | 1 |; ||x + y||² = 3² + 1² + 3² = 19;																							|
# |         | 3 |																															|
# |         -- --																															|
# | ||x||² + ||y||² = ||x + y||²;																											|
# |																																			|
# | xᵀy = 1 * 2 + 2 * -1 + 3 * 0 = 0;																										|
