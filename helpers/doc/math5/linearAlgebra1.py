# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 转置矩阵乘以矩阵与原矩阵之间的关系																											|
# | 对于 m * n 的矩阵 A, 存在公式:																												|
# | 零空间维度: dim( N( A ) ) + 矩阵的秩: rank( A ) = n;																						|
# |																																			|
# | 亦存在:																																	|
# | dim( N( A ) ) + rank( A ) = dim( N( AᵀA ) ) + rank( AᵀA );																				|
# |																																			|
# | 现在证明: rank( A ) = rank( AᵀA );																										|
# | 需要证明: dim( N( A ) ) = dim( N( AᵀA ) );																								|
# | 也就是: N( A ) = N( AᵀA );																												|
# | N( A ) = N( AᵀA ); -> dim( N( A ) ) = dim( N( AᵀA ) ); -> rank( A ) = rank( AᵀA );														|
# | 也就是通过证明矩阵 AᵀA 与矩阵 A 的零空间相同得到它们的维度相同得到 A 与 AᵀA 的秩相同;																|
# | 则矩阵 AᵀA 与矩阵 A 的零空间相同代表着 AᵀAx = 0 与 Ax = 0 的解 x 相同;																			|
# |																																			|
# | 推导 1: 若 Ax = 0 那么 AᵀAx = 0;																											|
# | 推导过程代表着对于任意 x, 若 x 为 Ax = 0 的解，则其必为 AᵀAx = 0 的解, 也就是零空间相同;															|
# |																																			|
# | 推导 2: 若 AᵀAx = 0 那么 xᵀAᵀAx = 0 -> (Ax)ᵀAx = 0 -> Ax = 0;																				|
# | 其中 Ax 表示的是一列向量, (Ax)ᵀAx 表示 Ax 列向量各元素的平方和;																					|
# | 推导过程代表着要使得 (Ax)ᵀAx = 0 成立, 则 Ax = 0 必成立, 也可理解为列向量 Ax 各元素的平方和为 0 ( (Ax)ᵀAx = 0 )必然 Ax 每个元素都为 0 ( Ax = 0 );		|
# | 故对于任意 x, 若 x 为 AᵀAx 的解, 则其必为 Ax = 0 的解, 也就是零空间相同;																		|
# | 结合推导 1, 推导 2 可得到 N( A ) = N( AᵀA ), 则 rank( A ) = rank( AᵀA );																	|
# |																																			|
# | 例:																																		|
# |       --     --   --   --   --    --                --    --																			|
# | AᵀA = | 1 1 1 | * | 1 1 | = | 3  8 |;  得到的对称矩阵	| 3  8 | 是可逆矩阵;																	|
# |       | 1 2 5 |   | 1 2 |   | 8 30 |                | 8 30 |																			|
# |       --     --   | 1 5 |   --    --                --    --																			|
# |                   --   --																												|
# | rank( Aᵀ ) = rank( A ) = rank( AᵀA ) = 2;																								|
# |																																			|
# | 注意: 不是所有的对称矩阵 AᵀA 都可逆;																											|
# |       --     --   --   --   --    --                --    --																			|
# | AᵀA = | 1 1 1 | * | 1 3 | = | 3  9 |;  得到的对称矩阵	| 3  9 | 不是可逆矩阵;																	|
# |       | 1 3 3 |   | 1 3 |   | 9 27 |                | 9 27 |																			|
# |       --     --   | 1 3 |   --    --                --    --																			|
# |                   --   --																												|
# | rank( Aᵀ ) = rank( A ) = rank( AᵀA ) = 1;																								|
# | 若 AᵀA 的零空间中仅有零向量时, 也就代表 A 零空间中仅有零向量, A 的各列线性无关;																	|
