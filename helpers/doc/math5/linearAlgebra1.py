# | 4 个基本子空间和维数总结																													|
# | 对于 m * n, 秩是 r 的矩阵 A 来说:																											|
# | 列空间: 位于 Rᵐ 空间( 行数代表列向量的分量: 维度数 ) 下的 r 维( 秩代表只有 r 个起作用的分量 )子空间;													|
# | 行空间: 位于 Rⁿ 空间( 列数代表 Aᵀ 列向量的分量: 维度数 ) 下的 r 维子空间;																		|
# | 零空间: 位于 Rⁿ 下的 n – r( n - r 代表 A 自由列 ) 维子空间;																					|
# | 左零空间: 位于 Rᵐ 下的 m – r( m - r 代表 Aᵀ 自由列 ) 维空间;																					|
