# |																																			|
# | 使用消元法得到行阶梯形式: 消元过程中零空间将不变的, 因为解始终为 0 未发生变化( Ax = 0 -> Ux = 0 );													|
# |     --          --    --          --																									|
# |     | 1  2  2  2 |    | 1  2  2  2 |																									|
# | U = | 2  4  6  8 | -> | 0  0  2  4 | 主元( 1, 1 ) 消除第二三的第一项																		|
# |     | 3  6  8 10 |    | 0  0  2  4 |																									|
