# | 	|	 第 3 - 3 项分解	  第 3 - 3 - 1 项	第 3 - 3 - 2 项	   第 3 - 3 - 3 项	|														|
# | 	|	|   0   0 a₁₃ |   |   0   0 a₁₃ |   |   0   0 a₁₃ |   |   0   0 a₁₃ |	|														|
# | 	|	|   0   0 a₂₃ | = |   0   0 a₂₃ | + |   0   0 a₂₃ | + |   0   0 a₂₃ |;	|														|
# | 	|	| a₃₁ a₃₂ a₃₃ |   | a₃₁   0   0 |   |   0 a₃₂   0 |   |   0   0 a₃₃ |	|														|
# | 	-----------------------------------------------------------------------------														|
# | 	-------------------------------------------------------------------------------------												|
# | 	| 根据性质 7, 存在全为 0 列的项, 则 Det(A) = 0, 剩余项为:									|												|
# | 	|      |   0   0 a₁₃ |																|												|
# | 	| Det( | a₂₁   0   0 | ) = +( a₁₃ * a₂₁ * a₃₂ ); 正号: 因为转化为对角矩阵需要进行两次变换;	|												|
# | 	|      |   0 a₃₂   0 |																|												|
# | 	|																					|												|
# | 	|      |   0   0 a₁₃ |																|												|
# | 	| Det( |   0 a₂₂   0 | ) = -( a₁₃ * a₂₂ * a₃₁ ); 负号;								|												|
# | 	|      | a₃₁   0   0 |																|												|
# | 	-------------------------------------------------------------------------------------												|
# |																																			|
# | 得到:																																	|
# | 3 阶矩阵分解共存在 3³ = 27 项, 消除 0 项后得到 3 的阶乘 3! = 6 项( 第一行存在 n 种可能, 第二行存在 n - 1 种可能, 以此类推得到共 n! ):					|
# |      | a₁₁ a₁₂ a₁₃ |     +( a₁₁ * a₂₂ * a₃₃ ) - ( a₁₁ * a₂₃ * a₃₂ )																		|
# | Det( | a₂₁ a₂₂ a₂₃ | ) = -( a₁₂ * a₂₁ * a₃₃ ) + ( a₁₂ * a₂₃ * a₃₁ )																		|
# |      | a₃₁ a₃₂ a₃₃ |     +( a₁₃ * a₂₁ * a₃₂ ) - ( a₁₃ * a₂₂ * a₃₁ )																		|
# |																																			|
# | 以此类推:																																	|
# | 4 阶矩阵分解共存在 4⁴ = 4 * 4 * 4 * 4 = 256, 消除 0 项后得到 4 的阶乘 4! = 4 * 3 * 2 * 1 = 24 项:												|
# |																																			|
# | 得到:																																	|
# | 因分解后每行仅存在一个非零项, 只需在其他行的相同列位置不存在非零项( 若存在则会导致行列式存在全零列 ), 保持这种规则则剩下的行列式都是非零的行列式:				|
# | Det( A ) = ∑( ± ( a₁ₕ * a₂ₖ * a₃៴ * ... * aₙₚ ) );																						|
# | 第一行的第 h 项 * 第二行的第 k 项 * 第三行的第 v 项 * ... * 第 n 行的第 p 项; h ≠ k ≠ v ≠ p;													|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 代数余子式																																|
# | 作用:																																	|
# | 代数余子式是从行列式的公式( Det( A ) = ∑( ± ( a₁ₕ * a₂ₖ * a₃៴ * ... * aₙₚ ) ) ) 中提取出来, 可将 n 阶行列式化简为 n - 1 阶行列式;					|
# |																																			|
# | 例:																																		|
# | 根据 Det( A ) = ∑( ± ( a₁ₕ * a₂ₖ * a₃៴ * ... * aₙₚ ) ) 分解三阶行列式:																		|
# |      | a₁₁ a₁₂ a₁₃ |																													|
# | Det( | a₂₁ a₂₂ a₂₃ | )																													|
# |      | a₃₁ a₃₂ a₃₃ |																													|
# |																																			|
# |   | a₁₁   0   0 |   |   0 a₁₂   0 |   |   0   0 a₁₃ |																					|
# | = |   0 a₂₂ a₂₃ | + | a₂₁   0 a₂₃ | + | a₂₁ a₂₂   0 |;																					|
# |   |   0 a₃₂ a₃₃ |   | a₃₁   0 a₃₃ |   | a₃₁ a₃₂   0 |																					|
# |																																			|
# | 	----------------------------------------------------------------------------														|
# | 	|                                                   | a₁₁   0   0 |																	|
# | 	| 比如选定第一行的一列, 然后考虑各种不会被消除为 0 的排列:	|   0 a₂₂ a₂₃ |;																|
# | 	|                                                   |   0 a₃₂ a₃₃ |																	|
# | 	| 注意: 根据第任何一行或列展开都将得到同样的结果;																							|
# | 	----------------------------------------------------------------------------														|
# |																																			|
# | = a₁₁ | a₂₂ a₂₃ | + a₁₂ | a₂₁ a₂₃ | + a₁₃ | a₂₁ a₂₂ |																					|
# |       | a₃₂ a₃₃ |       | a₃₁ a₃₃ |       | a₃₁ a₃₂ |																					|
# |																																			|
# | 	----------------------------------------------------------------------------														|
# | 	| 计算被选定排除后的 n - 1 阶矩阵行列式																									|
# | 	----------------------------------------------------------------------------														|
# |																																			|
# | = + a₁₁ * ( a₂₂ * a₃₃ - a₂₃ * a₃₂ )																										|
# |   - a₁₂ * ( a₂₁ * a₃₃ - a₂₃ * a₃₁ )																										|
# |   + a₁₃ * ( a₂₁ * a₃₂ - a₂₂ * a₃₁ );																									|
# |																																			|
# | 	----------------------------------------------------------------------------														|
# | 	| 括号中由剩余因子组成的表达式就是代数余子式, 比如:																							|
# | 	| a₁₁ 的代数余子式是 a₂₂ * a₃₃ - a₂₃ * a₃₂;																							|
# | 	| a₁₂ 的代数余子式是 a₂₁ * a₃₃ - a₂₃ * a₃₁, 但符号应是负号, 即 -( a₂₁ * a₃₃ - a₂₃ * a₃₁ );												|
# | 	| 化简 a₁₂ 的代数余子式是 a₂₃ * a₃₁ - a₂₁ * a₃₃;																						|
# | 	----------------------------------------------------------------------------														|
# |																																			|
# | = + a₁₁ * ( a₂₂ * a₃₃ - a₂₃ * a₃₂ )																										|
# |   + a₁₂ * ( a₂₃ * a₃₁ - a₂₁ * a₃₃ )																										|
# |   + a₁₃ * ( a₂₁ * a₃₂ - a₂₂ * a₃₁ );																									|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 代数余子式的符号:																															|
# |																																			|
# | 代数余子式正负号需要单独判断, 判断方法是根据选定元素行号和列号之和的奇偶性:																			|
# | 当 i + j 是偶数时, 行列式取正号;																											|
# | 当 i + j 是奇数时, 行列式取负号;																											|
# |																																			|
# | 比如三阶行列式中, 用 Cᵢⱼ 表示 aᵢⱼ 的代数余子式, C₁₂ 的行列号之和是 1 + 2 = 3, 则其对应的代数余子式取负号												|
# | 例:																																		|
# |                                     | + - + - + |																						|
# |                                     | - + - + - |																						|
# | 五阶行列式的每项的代数余子式的符号分布如:	| + - + - + |;																						|
# |                                     | - + - + - |																						|
# |                                     | + - + - + |																						|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 代数余子式公式:																															|
# | Det( A ) = a₁₁C₁₁ + a₁₂C₁₂ + ... + a₁ₙC₁ₙ;																								|
# | 可根据该公式, 将 n 阶行列式转化为 n - 1 阶, 然后在转化为 n - 2 阶, 循环转化直到转化为 1 阶行列式从而求得 n 阶行列式;									|
# |																																			|
# | 例:																																		|
# | 用代数余子式可以解释二阶行列式的计算公式																										|
# | | a₁₁ a₁₂ |																																|
# | | a₂₁ a₂₂ |																																|
# |																																			|
# | = a₁₁C₁₁ + a₁₂C₁₂																														|
# |																																			|
# | = a₁₁ * | 0   0 | - a₁₂ * |   0 0 |																										|
# |         | 0 a₂₂ |         | a₂₁ 0 |																										|
# |																																			|
# | = a₁₁ * a₂₂ - a₁₂ * a₂₁;																												|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 逆矩阵公式以及伴随矩阵																														|
# | 例:																																		|
# | 若 A 为二阶矩阵, A 的逆矩阵公式 A⁻¹:																											|
# |     --       --                           --         --																					|
# | A = | a₁₁ a₁₂ |; A⁻¹ = ( 1 / Det( A ) ) * |  a₂₂ -a₁₂ |;																				|
# |     | a₂₁ a₂₂ |                           | -a₂₁  a₁₁ |																					|
# |     --       --                           --         --																					|
# |																																			|
# | 其中:																																	|
# |                       --     --																											|
# | a₁₁ 的代数余子式: C₁₁ = | 0   0 | = +a₂₂;																									|
# |                       | 0 a₂₂ |																											|
# |                       --     --																											|
# |                       --     --																											|
# | a₁₂ 的代数余子式: C₁₂ = |   0 0 | = -a₂₁; 注意 i + j = 3 为奇数, 代数余子式符号为负号;															|
# |                       | a₂₁ 0 |																											|
# |                       --     --																											|
# |                       --     --																											|
# | a₂₁ 的代数余子式: C₂₁ = | 0 a₁₂ | = -a₁₂;																									|
# |                       | 0   0 |																											|
# |                       --     --																											|
# |                       --     --																											|
# | a₂₂ 的代数余子式: C₂₂ = | a₁₁ 0 | = +a₁₁;																									|
# |                       | 0   0 |																											|
# |                       --     --																											|
# |                     --         --																										|
# | 得到代数余子式矩阵:	C = |  a₂₂ -a₂₁ |; 由代数余子式构成的矩阵;																					|
# |                     | -a₁₂  a₁₁ |																										|
# |                     --         --																										|
# |																																			|
# |                      --         --																										|
# | 则二阶逆矩阵公式中的矩阵 |  a₂₂ -a₁₂ | 是代数余子式矩阵的转置 Cᵀ 即伴随矩阵, 则公式可记作:															|
# |                      | -a₂₁  a₁₁ |																										|
# |                      --         --																										|
# | A⁻¹ = ( 1 / Det( A ) ) * Cᵀ, 代表: A 的逆矩阵等于伴随矩阵除以 A 的行列式;																		|
# |																																			|
# | 推导:																																	|
# | 需证明公式 A⁻¹ = ( 1 / Det( A ) ) * Cᵀ 是否成立:																							|
# |																																			|
# | 转化:																																	|
# | A * A⁻¹ = A * ( 1 / Det( A ) ) * Cᵀ;																									|
# | I = ( A * Cᵀ ) / Det( A );																												|
# |																																			|
# | 若证明该等式成立即证明公式成立:																												|
# | A * Cᵀ = Det( A ) * I;																													|
# |																																			|
# | 将 A 与 Cᵀ 用矩阵形式表示:																													|
# | --						--   --						--																					|
# | |	A₁₁	A₁₂	A₁₃	...	A₁ₙ	 |   |	C₁₁	C₂₁	C₃₁	...	Cₙ₁	 |																					|
# | |	A₂₁	A₂₂	A₂₃	...	A₂ₙ	 | * |	C₁₂	C₂₂	C₃₂	...	Cₙ₂	 |																					|
# | |	...					 |   |	...					 |																					|
# | |	Aₙ₁	Aₙ₂	Aₙ₃	...	Aₙₙ	 |   |	C₁ₙ	C₂ₙ	C₃ₙ	...	Cₙₙ	 |																					|
# | --						--   --						--																					|
# |    --																									   --							|
# |    |	A₁₁C₁₁ + A₁₂C₁₂ + ... + A₁ₙC₁ₙ	A₁₁C₂₁ + A₁₂C₂₂ + ... + A₁ₙC₂ₙ	...	A₁₁Cₙ₁ + A₁₂Cₙ₂ + ... + A₁ₙCₙₙ	|							|
# | =  |	A₂₁C₁₁ + A₂₂C₁₂ + ... + A₂ₙC₁ₙ	A₂₁C₂₁ + A₂₂C₂₂ + ... + A₂ₙC₂ₙ	...	A₂₁Cₙ₁ + A₂₂Cₙ₂ + ... + A₂ₙCₙₙ	|							|
# |    |	...																									|							|
# |    |	Aₙ₁C₁₁ +  Aₙ₂C₁₂ + ... + AₙₙC₁ₙ	Aₙ₁C₂₁ +  Aₙ₂C₂₂ + ... + AₙₙC₂ₙ	...	AₙₙCₙₙ  + AₙₙCₙₙ  + ... + AₙₙCₙₙ		|							|
# |    --																									   --							|
# |																																			|
# |    化简: 根据代数余子式公式可得到的矩阵对角线位置的结果为行列式的代数余子式的表达式即 Det( A ), 而其他位置为 0,											|
# |    因为在对角线位置的表示式满足代数余子式公式, 而不在对角线位置的项比如: A₁₁C₂₁ + A₁₂C₂₂ + ... + A₁ₙC₂ₙ 的结果必然为 0;								|
# |    --                              --																									|
# |    | Det( A )        0 ...        0 |																									|
# | =  |        0 Det( A ) ...        0 |;																									|
# |    | ...                            |																									|
# |    |        0        0 ... Det( A ) |																									|
# |    --                              --																									|
# |																																			|
# | = Det( A ) * I;																															|
# |																																			|
# | 结论:																																	|
# | 公式 A⁻¹ = ( 1 / Det( A ) ) * Cᵀ 成立;																									|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 克莱姆法则																																|
# | 将逆矩阵公式代入 Ax = b 中得到:																												|
# | Ax = b;																																	|
# | A⁻¹Ax = A⁻¹b;																															|
# | x = A⁻¹ * b = ( 1 / Det( A ) ) * Cᵀ * b;																								|
# |																																			|
# | 观察得到:																																	|
# | 1: 公式中分母位置为行列式的值 Det( A ), 说明方程组的解 x 中存在 Det( A ) 的参与;																	|
# | 2: 公式中分子位置为 Cᵀ * b, C 转置中的代数余子式乘以 b 的各个分量可理解为某矩阵的行列式的值;															|
# |																																			|
# | 方程组的解 x 的第一个分量 x₁: Det( B₁ ) / Det( A );																							|
# | 方程组的解 x 的第二个分量 x₂: Det( B₂ ) / Det( A );																							|
# |																																			|
# | 推算 B 矩阵:																																|
# | B₁ 矩阵: b 向量的第一列乘以 Cᵀ 的第一行, 则行列式为 Det( B₁ ) = b₁ * C₁₁ + b₂ * C₂₁ + ... + bₙ * Cₙ₁ 的 B₁ 矩阵形如:								|
# |      --						--																											|
# |      |	b₁	A₁₂	A₁₃	...	A₁ₙ	 |																											|
# | B₁ = |	b₂	A₂₂	A₂₃	...	A₂ₙ	 |; 把 A 矩阵的第一列用 b 向量代替;																				|
# |      |	...					 |																											|
# |      |	bₙ	Aₙ₂	Aₙ₃	...	Aₙₙ	 |																											|
# |      --						--																											|
# |																																			|
# | B₂ 矩阵: b 向量的第一列乘以 Cᵀ 的第二行, 则行列式为 Det( B₂ ) = b₁ * C₁₂ + b₂ * C₂₂ + ... + bₙ * Cₙ₂ 的 B₂ 矩阵形如:								|
# |      --						--																											|
# |      |	A₁₂	b₁	A₁₃	...	A₁ₙ	 |																											|
# | B₂ = |	A₂₂	b₂	A₂₃	...	A₂ₙ	 |; 把 A 矩阵的第二列用 b 向量代替;																				|
# |      |	...					 |																											|
# |      |	Aₙ₂	bₙ	Aₙ₃	...	Aₙₙ	 |																											|
# |      --						--																											|
# |																																			|
# | 依此类推:																																	|
# | ... ...;																																|
# |																																			|
# | 若转化矩阵形式:																															|
# | --   --                      --						--   --    --																		|
# | | x₁  |                      |	C₁₁	C₂₁	C₃₁	...	Cₙ₁	 |   |	b₁	|																		|
# | | x₂  | = ( 1 / Det( A ) ) * |	C₁₂	C₂₂	C₃₂	...	Cₙ₂	 | * |	b₂	|;																		|
# | | ... |                      |	...					 |   |	...	|																		|
# | | xₙ	  |                      |	C₁ₙ	C₂ₙ	C₃ₙ	...	Cₙₙ	 |   |	bₙ	|																		|
# | --   --                      --						--   --    --																		|
# |																																			|
# | 得到:																																	|
# | --   --                        --									   --																|
# | | x₁  |                        |  b₁ * C₁₁ + b₂ * C₂₁ + ... + bₙ * Cₙ₁	|																|
# | | x₂  |  =  ( 1 / Det( A ) ) * |  b₁ * C₁₂ + b₂ * C₂₂ + ... + bₙ * Cₙ₂	|;																|
# | | ... |                        |  ...								    |																|
# | | xₙ	  |                        |  b₁ * C₁ₙ + b₂ * C₂ₙ  + ... + bₙ * Cₙₙ	|																|
# | --   --                        --									   --																|
# |																																			|
# | 结论:																																	|
# | 现在已知 B 矩阵和 A 矩阵 然后依次计算便能得到每个分量的结果, 不过计算量太大, 不建议使用;																|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 特征向量																																	|
# | 理解:																																	|
# | 函数 f 作用在 x 上得到 f( x ), 矩阵 A 的作用类似函数 f, 参数为向量 x, 通过 A 的作用得到向量 Ax;													|
# | 对多数向量 x 经过 A 的转换后将得到不同方向的向量, 但存在一些特殊的参数 x 与结果向量 Ax 方向相同, 即:													|
# | Ax 平行于 x, 特殊的参数向量 x 就是矩阵 A 的特征向量, 表示为: Ax = λx;																			|
# | 其中:																																	|
# | A: 方阵;																																	|
# | x: x ≠ 0, 矩阵 A 的特征向量;																												|
# | λ: 被称为特征系数或特征值的一个系数;																											|
# |																																			|
# | 注意:																																	|
# | x 和 Ax 方向相同包括正方向, 反方向, 无方向, 故 λ 的值可取 0 或负数;																				|
# |																																			|
# | 对于特征值为 0 的情况, 即:																													|
# | Ax = 0x = 0; 特征向量 x 也就是 A 的零空间的向量;																								|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 投影矩阵的特征向量																															|
# | 例:																																		|
# | 假设存在平面, x 向量在平面的投影是 Px, 二者的方向不相同, 因此 x 不是 P 的特征向量;																	|
# | 若 x 正好趴在平面上, 则其投影就是 x 同方向或反方向的向量, 故位于平面上的所有向量都是 P 的特征向量, 即:													|
# | 特征值 λ = 1, Px = x;																														|
# | 特征值 λ = -1, Px = -x;																													|
# |																																			|
# | 若 x 正好垂直于平面, 则其投影就是零向量, 故垂直于平面的向量都是 P 的特征向量, 即:																	|
# | 特征值 λ = 0, Px = 0x = 0;																												|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 矩阵的迹																																	|
# | 例:																																		|
# |                 --   --																													|
# | 若存在置换矩阵	A = | 0 1 |, 可容易看出:																										|
# |                 | 1 0 |																													|
# |                 --   --																													|
# | --   --   -- --   -- --              -- --																								|
# | | 0 1 | * | 1 | = | 1 |, 得到向量	x₁ = | 1 | 为特征向量, λ₁ = 1, Ax = x;																	|
# | | 1 0 |   | 1 |   | 1 |              | 1 |																								|
# | --   --   -- --   -- --              -- --																								|
# | --   --   --  --   --  --               --  --																							|
# | | 0 1 | * | -1 | = |  1 |, 得到向量 x₂ =	| -1 | 为特征向量, λ₂ = -1, Ax = -x;																|
# | | 1 0 |   |  1 |   | -1 |               |  1 |																							|
# | --   --   --  --   --  --               --  --																							|
# | --   --   -- --   -- --              -- --																								|
# | | 0 1 | * | 0 | = | 0 |, 得到向量	x₃ = | 0 | 为特征向量, λ₃ = 0, Ax = 0, 零空间中只有零向量;													|
# | | 1 0 |   | 0 |   | 0 |              | 0 |																								|
# | --   --   -- --   -- --              -- --																								|
# |																																			|
# | 得到																																		|
# | 1: x₁ 与 x₂ 垂直;																															|
# | 2: 零向量没有什么价值所以特征向量一般排除零向量, 因为任意矩阵都存在零空间这个特征向量和 0 这个特征值;													|
# |																																			|
# | 上面的答案符合两个关于特征值的性质:																											|
# | 1: n * n 矩阵有 n 个特征值;																												|
# | 2: 矩阵的所有特征值之和等于该矩阵的主对角线元素之和, 此和数叫做 A 的迹;																			|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 特征方程																																	|
# | 特征方程推导:																																|
# | 对于:																																	|
# | Ax = λx;																																|
# |																																			|
# | 转化:																																	|
# | Ax - λx = 0;																															|
# |																																			|
# | 使用 λx = λIx 将 λ 向量化:																													|
# | Ax - λIx = 0;																															|
# |																																			|
# | 合并:																																	|
# | (A - λI)x = 0;																															|
# | 可看出相当于求解 A - λI 矩阵的零空间, 对于不为零向量的 x 在等式中说明 A - λI 为奇异矩阵;															|
# |																																			|
# | 则得到了关于 λ 的方程称为特征方程或特征值方程:																									|
# | Det( A - λI ) = 0;																														|
# | 根据特征方程求解出 λ, 对于 n 阶方阵会求出 n 个 λ, 再把每个 λ 代入 ( A - λI )x = 0 求出零空间, 得到特征向量;											|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 根据特征方程求解特征值与特征向量																												|
# | 例:																																		|
# |         --   --																															|
# | 计算	A = | 3 1 | 的特征向量和特征值;																											|
# |         | 1 3 |																															|
# |         --   --																															|
# |																																			|
# | 代入 Det( A - λI ) = 0 得到:																												|
# |      --   --       --   --          --   --   --   --          --           --															|
# | Det( | 3 1 | - λ * | 1 0 | ) = Det( | 3 1 | - | λ 0 | ) = Det( | 3 - λ     1 | ) = 0;													|
# |      | 1 3 |       | 0 1 |          | 1 3 |   | 0 λ |          |     1 3 - λ |															|
# |      --   --       --   --          --   --   --   --          --           --															|
# |																																			|
# | 得到:																																	|
# | ( 3 - λ )² - 1 = 0;																														|
# |																																			|
# | 得到特征值:																																|
# | λ₁ = 2;																																	|
# | λ₂ = 4;																																	|
# |																																			|
# | 使用矩阵 A 的迹验证:																														|
# | 特征值之和为矩阵 A 的迹 = 2 + 4 = 6;																										|
# | 矩阵 A 的主对角线之和 = 3 + 3 = 6;																											|
# | 特征值之和为矩阵 A 的迹 = 矩阵 A 的主对角线之和 = 6;																							|
# |																																			|
# | 将特征值 λ₁ 代回方程 (A - λI)x = 0:																										|
# |   --   --       --   --        --           --        --   --																			|
# | ( | 3 1 | - 2 * | 1 0 | )x = ( | 3 - 2     1 | )x = ( | 1 1 | )x = 0;																	|
# |   | 1 3 |       | 0 1 |        |     1 3 - 2 |        | 1 1 |																			|
# |   --   --       --   --        --           --        --   --																			|
# |         --   --             --  --                          --  --																		|
# | 求得矩阵	| 1 1 | 的零空间的基为	|  1 |; 则特征值 λ₁ 对应的特征向量:	|  1 |, 实际上零空间中的所有向量都是 λ₁ 对应的特征向量;								|
# |         | 1 1 |             | -1 |                          | -1 |																		|
# |         --   --             --  --                          --  --																		|
# |																																			|
# | 将特征值 λ₂ 代回方程 (A - λI)x = 0:																										|
# |   --   --       --   --        --           --        --     --																			|
# | ( | 3 1 | - 4 * | 1 0 | )x = ( | 3 - 4     1 | )x = ( | -1  1 | )x = 0;																	|
# |   | 1 3 |       | 0 1 |        |     1 3 - 4 |        |  1 -1 |																			|
# |   --   --       --   --        --           --        --     --																			|
# |         --     --           -- --                           -- --																		|
# | 求得矩阵	| -1  1 | 的零空间的基	| 1 |; 则特征值 λ₂ 对应的特征向量:	| 1 |, 实际上零空间中的所有向量都是 λ₂ 对应的特征向量;								|
# |         |  1 -1 |           | 1 |                           | 1 |																		|
# |         --     --           -- --                           -- --																		|
# |																																			|
# | 结论:																																	|
# |     --   --                             --  --                              -- --														|
# | A = | 3 1 | 的特征值 λ₁ = 2 对应的特征向量为	|  1 |, 特征值 λ₂ = 4 对应的特征向量为	| 1 |;														|
# |     | 1 3 |                             | -1 |                              | 1 |														|
# |     --   --                             --  --                              -- --														|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 矩阵变换对特征值关系																														|
# | 例:																																		|
# | 对于:																																	|
# |         --   --                             --  --                              -- --													|
# | 矩阵 A:	| 3 1 | 的特征值 λ₁ = 2 对应的特征向量为|  1 |, 特征值 λ₂ = 4 对应的特征向量为	| 1 |;													|
# |         | 1 3 |                             | -1 |                              | 1 |													|
# |         --   --                             --  --                              -- --													|
# |         --   --                               --  --                            -- --													|
# | 矩阵 B:	| 0 1 | 的特征值 λ₁ = -1 对应的特征向量为 |  1 |, 特征值 λ₂ = 1 对应的特征向量	| 1 |;													|
# |         | 1 0 |                               | -1 |                            | 1 |													|
# |         --   --                               --  --                            -- --													|
# |																																			|
# | 得到矩阵间的关系:																															|
# | A = B + 3 * I;																															|
# |																																			|
# | 得到特征值的关系:																															|
# | A 的特征值 = B 的特征值 + 3;																												|
# |																																			|
# | 得到特征向量的关系:																															|
# | A 的特征向量 = B 的特征向量;																												|
# |																																			|
# | 推导:																																	|
# | Ax = λx;																																|
# | Ix = 1;																																	|
# | ( A + nI )x = Ax + nIx = λx + n = ( λ + n )x;																							|
# |																																			|
# | 代表:																																	|
# | 矩阵 A 加上 n 被单位矩阵, 其结果矩阵的特征值等于 A 的特征值 + n, 而特征向量 x 则没有变化;															|
# |																																			|
# | 推导 A 加上任意已知特征值 α 的矩阵 C 其结果对比 A 的特征值:																						|
# |																																			|
# | 对于 A 与 C 的特征向量相同时:																												|
# | Ax = λx;																																|
# | Cx = αx;																																|
# | ( A + C )x = Ax + Cx = λx + αx = ( λ + α )x;																							|
# |																																			|
# | 得到:																																	|
# | 若已知任意两矩阵的特征值且两矩阵特征向量相同时, 两矩阵相加得到的矩阵特征值就是两矩阵的特征值之和;														|
# | 而大部分情况任意两矩阵的特征向量不相同, 所以特征值并不存在类似线性关系或乘积相关的特质;																|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 特征值乘积与行列式																															|
# | 推导:																																	|
# | 在 A - λI 矩阵代出对角线上的 λ 后, 表现为:																									|
# | Det( A - λI ) = a₀ + a₁λ + a₂λ² + a₃λ³ + ... + aₙλⁿ;																						|
# |																																			|
# | 令 λ = 0 得到矩阵 A 的行列式:																												|
# | Det( A - 0I ) = Det( A ) = a₀;																											|
# |																																			|
# | 矩阵的特征值就是这个特征多项式的根:																											|
# | λ₁, λ₂, λ₃, λ₄ ... λₙ;																													|
# |																																			|
# | 根据韦达定理( 两个根的和与乘积能够直接通过方程系数计算 ):																						|
# | λ₁ * λ₂ * λ₃ * λ₄ * ... * λₙ = a₀ = Det( A );																							|
# |																																			|
# | 结论:																																	|
# | 各特征值乘积与行列式相等;																													|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 复数特征值																																|
# | 例:																																		|
# |                               --    --																									|
# | 特征值未必是实数, 比如旋转矩阵 A	= | 0 -1 |, 即向量在矩阵的作用下旋转 90 度后还能和自身平行;															|
# |                               | 1  0 |																									|
# |                               --    --																									|
# |                      --           --																									|
# | Det( A - λI ) = Det( | 0 - λ    -1 | ) = λ² + 1 = 0;																					|
# |                      |     1 0 - λ |																									|
# |                      --           --																									|
# | 得到复数特征值:																															|
# | λ₁ = i, λ₂ = -i;																														|
# |																																			|
# | 验证:																																	|
# | 复数特征值 λ₁, λ₂ 满足 A 的行列式: Det( A ) = 0 - (-1) = 1 = λ₁ * λ₂;																		|
# | 复数特征值 λ₁, λ₂ 满足 A 的迹: 0 + 0 = λ₁ + λ₂ = 0;																							|
# |-----------------------------------------------------------------------------------------------------------------------------------------|

# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 对称矩阵的特征向量正交																														|
# | 若 x₁, x₂ 是对称矩阵 A 的两个特征向量, 即:																									|
# | Ax₁ = λ₁x₁;																																|
# | Ax₂ = λ₂x₂;																																|
# |																																			|
# | 则必存在不同特征值的特征向量一定正交, 即:																										|
# | x₁ᐪx₂ = 0;																																|
# |																																			|
# | 推导:																																	|
# | 假设 z = x₁ᐪλ₂x₂ = x₁ᐪAx₂, 行向量乘以列向量得到标量, 即:																						|
# | zᐪ = z;																																	|
# |																																			|
# | 也就是:																																	|
# | (x₁ᐪAx₂)ᐪ = x₂ᐪAᐪx₁ = x₁ᐪAx₂;																												|
# |																																			|
# | 又因为 A 是对称矩阵, 即:																													|
# | Aᐪ = A;																																	|
# |																																			|
# | 所以得到:																																	|
# | x₂ᐪAᐪx₁ = x₂ᐪAx₁;																															|
# |																																			|
# | 将 x₂ᐪAᐪx₁ = x₂ᐪAx₁ 代入 x₂ᐪAᐪx₁ = x₁ᐪAx₂, 得到:																							|
# | x₂ᐪAx₁ = x₁ᐪAx₂;																															|
# |																																			|
# | 将 Ax₂ = λ₂x₂ 代入 x₂ᐪAx₁ = x₁ᐪAx₂, 得到:																									|
# | x₁ᐪAx₂ = x₁ᐪλ₂x₂ = λ₂x₁ᐪx₂;																												|
# |																																			|
# | 将 Ax₁ = λ₁x₁ 代入 x₂ᐪAx₁ = x₁ᐪAx₂, 得到:																									|
# | x₂ᐪAx₁ = x₂ᐪλ₁x₁ = λ₁x₂ᐪx₁;																												|
# |																																			|
# | 结合得到:																																	|
# | z = λ₁x₂ᐪx₁ = λ₂x₁ᐪx₂;																													|
# |																																			|
# | 再代入 zᐪ = z, 得到:																														|
# | (λ₁x₂ᐪx₁)ᐪ = λ₁x₁ᐪx₂ = λ₂x₁ᐪx₂;																											|
# |																																			|
# | 化简得到:																																	|
# | λ₁x₁ᐪx₂ - λ₂x₁ᐪx₂ = 0;																													|
# | (λ₁ - λ₂)x₁ᐪx₂ = 0;																														|
# |																																			|
# | 因为 λ₁, λ₂ 是矩阵的两个不同的特征值, 即:																										|
# | λ₁ ≠ λ₂;																																|
# |																																			|
# | 故 x₁ 向量与 x₂ 向量垂直:																													|
# | x₁ᐪx₂ = 0;																																|
# |																																			|
# | 结论:																																	|
# | 对于对称矩阵其不同特征值的特征向量必然正交;																										|
# |-----------------------------------------------------------------------------------------------------------------------------------------|


