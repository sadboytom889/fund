# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 列空间的基																																|
# | 性质:																																	|
# | 1: 各列线性无关;																															|
# | 2: 它们能张成整个空间;																														|
# |																																			|
# | 例:																																		|
# |																																			|
# | 分析:																																	|
# |         --       --                                 -- --  -- --  -- --  -- --															|
# |         | 1 1 1 1 |                                 | 1 |  | 1 |  | 1 |  | 1 |															|
# | 矩阵 A:	| 2 1 4 3 | 的列空间 C( A ) 不能表示为 span(	| 2 |, | 1 |, | 4 |, | 3 | );														|
# |         | 3 4 1 2 |                                 | 3 |  | 4 |  | 1 |  | 2 |															|
# |         --       --                                 -- --  -- --  -- --  -- --															|
# | 因为对于矩阵 A 的四个列向量还需检查是否线性相关, 简单看来 C( A ) 最多能张成 R³ 空间;																|
# | 而对于 R³ 空间只需三个向量就够了, 而 A 的四个向量中至少有一个是多余的;																				|
# |																																			|
# | 计算最简行阶梯形式 R:																														|
# |                 --       --    --            --    --             --																	|
# |                 | 1 1 1 1 |    | (1)  1  1  1 |    | (1)   0  3  2 |																	|
# | 消元化简得到 R =	| 2 1 4 3 | -> |  0  -1  2  1 | -> |  0  (-1) 2  1 |																	|
# |                 | 3 4 1 2 |    |  0   1 -2 -1 |    |  0    0  0  0 |																	|
# |                 --       --    --            --    --             --																	|
# | 根据主元判断: R( A ) = 2; 且 x₁, x₂ 是主列( 线性孤立 ), x₃, x₄ 是自由列( 线性相关 );															|
# |                                                 -- --  -- --																			|
# |                                                 | 1 |  | 1 |																			|
# | 列空间的基用可用主元所在的列数表示; C( A ) = span(	| 2 |, | 1 |    );																		|
# |                                                 | 3 |  | 4 |																			|
# |                                                 -- --  -- --																			|
# |																																			|
# | 分析得到:																																	|
# | 1: 在讨论 Rⁿ 空间, 那么基向量肯定是 n 个; 否则数量不足以支撑 Rⁿ 空间;																			|
# |																																			|
# | 2: Rⁿ 中 n * n 方阵 A 若要满足 n 个向量够成基, 那么必定 A 是可逆的;																				|
# | 因为 n 个向量够成基代表没有线性相关, 至少是列满秩, 又因为是方阵, 所以必定是满秩矩阵, 满秩必定是可逆的;													|
# |																																			|
# | 3: 矩阵 A 的基的个数和秩相等, 因为秩的定义就是主列的个数;																						|
# | 同时秩也是矩阵列空间能张成的维数( 注意不是矩阵维数, 矩阵维数也许大于其列空间张成空间的维数, 如存在线性相关 );											|
