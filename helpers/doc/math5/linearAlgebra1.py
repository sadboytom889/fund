# | 正交子空间																																|
# | 定义:																																	|
# | 若存在两个子空间 S, T; S 中的每个向量与 T 中每个向量都正交, 则 S 和 T 正交;																		|
# |																																			|
# | 例1:																																		|
# | 一堵墙和地面不正交( 假设墙上的画一条不水平与地面的线不与地面正交 );																				|
# | 事实上, 若两个子空间在某向量处相交, 那它们一定不正交, 因为相交处的向量处与两个子空间而其不能正交与本身,													|
# | 也就是说: 若两个子空间交于一个非零向量, 则两个子空间一定不会正交;																					|
# | 换句话说: 若两个子空间正交, 它们一定不会交于某个非零向量, 且只能交于零向量( 单独的点就是零向量, 没有方向, 或是有任意方向且模长为 0 ); 						|
# | 同时也说明: 两个平面垂直并不等同于两个平面(子空间)正交, 正交一定垂直, 垂直不一定正交;																|
# |																																			|
# | 例2:																																		|
# | 由于若 V 是 Rⁿ 的线性子空间, 则 V 一定满足三个条件:																							|
# | 1: 包含 0 向量;																															|
# | 2: x 是空间 V 中的一个向量, x 和一个标量的乘积也在 V 中, 即数乘封闭性;																			|
# | 3: a 和 b 是空间 V 中的向量, a + b 在 V 中, 即加法封闭性;																						|
# |																																			|
# | 则平面内只有三个子空间: 原点, 过原点的直线, 整个平面; 平面中的子空间正交关系存在:																	|
# | 1: 过原点的直线任何时候都不会和整个平面正交;																									|
# | 2: 原点和所有过原点的直线正交, 也和整个平面正交;																								|
# | 3: 若两个过原点的向量的点积是 0, 二者正交;																									|
