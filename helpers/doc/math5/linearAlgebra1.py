# |-----------------------------------------------------------------------------------------------------------------------------------------|
# | 4 个基本子空间的正交关系																													|
# | 1: 行空间与零空间存在正交关系																												|
# | 推导:																																	|
# | 零空间代表:																																|
# | Ax = 0;																																	|
# | --                     --   --     --   --    --    --      --   --    --   --    --													|
# | |  A₁₁ A₁₂ A₁₃ ... A₁ₙ	|   |   x₁  |   |  0   |    |  row₁  |   |  x₁  |   |  0   |													|
# | |  A₂₁ A₂₂ A₂₃ ... A₂ₙ	|   |   x₂  |   |  0   |    |  row₂  |   |  x₂  |   |  0   |													|
# | |  A₃₁ A₃₂ A₃₃ ... A₃ₙ	| * |   x₃  | = |  0   | -> |  row₃  | * |  x₃  | = |  0   |;													|
# | |  ...               	|   |   ... |   |  ... |    |  ...   |   |  ... |   |  ... |													|
# | |  Aₘ₁ Aₘ₂ Aₘ₃ ... Aₘₙ	|   |   xₘ	|   |  0   |    |  rowₘ  |   |  xₘ	|   |  0   |													|
# | --                     --   --     --   --    --    --      --   --    --   --    --													|
# | A₁₁x₁ + A₁₂x₂ + A₁₃x₃ + ... + A₁ₙxₘ = 0; -> row₁ᵀx = 0; -> 第一行行向量与向量 x 正交;														|
# | A₂₁x₁ + A₂₂x₂ + A₂₃x₃ + ... + A₂ₙxₘ = 0; -> row₂ᵀx = 0; -> 第二行行向量与向量 x 正交;														|
# | A₃₁x₁ + A₃₂x₂ + A₃₃x₃ + ... + A₃ₙxₘ = 0; -> row₃ᵀx = 0; -> 第三行行向量与向量 x 正交;														|
# | ...																																		|
# | Aₘ₁x₁ + Aₘ₂x₂ + Aₘ₃x₃ + ... + Aₘₙxₘ = 0; -> rowₘᵀx = 0; -> 第 m 行行向量与向量 x 正交;														|
# | 得到: A 中的每个行向量都正交于零空间中的 x;																									|
# |																																			|
# | 验证乘法和加法是否满足:																														|
# | rowₛᵀx = 0; -> c₁ * rowₛᵀx = 0;																											|
# | rowₚᵀx = 0; -> c₂ * rowₚᵀx = 0;																											|
# | ( c₁ * rowₛ + c₂ * rowₚ )ᵀx = 0;																											|
# |																																			|
# | 结论:																																	|
# | 乘法和加法满足条件( xᵀy = 0 )代表 x 向量( 零空间 )正交于所有行向量张成的空间( 行空间 );															|
# |																																			|
# | 其次:																																	|
# | 对于 m * n, 秩是 r 的矩阵 A 来说, 零空间和行空间: 把 Rⁿ 空间分割成 r 维子空间( 列空间 )和 n – r 维子空间( 零空间 );									|
# |																																			|
# | 2: 列空间与左零空间存在正交关系																												|
# | 推导:																																	|
# | 上述对矩阵 A 推导过程转化为 A 的转置 Aᵀ, 依然有效;																								|
# | Aᵀx = 0;																																|
# | --                     --   --    --   --    --    --      --   --    --   --    --														|
# | | A₁₁  A₂₁  A₃₁ ... Aₙ₁	|   |  x₁  |   |  0   |    |  row₁  |   |  x₁  |   |  0   |														|
# | | A₁₂  A₂₂  A₃₂ ... Aₙ₂	|   |  x₂  |   |  0   |    |  row₂  |   |  x₂  |   |  0   |														|
# | | A₁₃  A₂₃  A₃₃ ... Aₙ₃	| * |  x₃  | = |  0   | -> |  row₃  | * |  x₃  | = |  0   |;													|
# | | ...                   |   |  ... |   |  ... |    |  ...   |   |  ... |   |  ... |														|
# | | A₁ₙ  A₂ₙ  A₃ₙ ...  Aₙₘ	|   |  xₘ  |   |  0   |    |  rowₘ  |   |  xₘ  |   |  0   |														|
# | --                     --   --    --   --    --    --      --   --    --   --    --														|
# |																																			|
# | 结论:																																	|
# | 乘法和加法满足条件( xᵀy = 0 )代表 x 向量( 左零空间 )正交于所有列向量张成的空间( 列空间 );															|
# |																																			|
# | 其次:																																	|
# | 对于 m * n, 秩是 r 的矩阵 A 来说, 左零空间和列空间: 把 Rᵐ 空间分割成 r 维子空间( 列空间 )和 m – r 维子空间( 左零空间 );								|
# |																																			|
# | 例:																																		|
# | Ax = 0;																																	|
# | --         --   --  --   -- --																											|
# | |  1  3   5 | * | x₁ | = | 0 |																											|
# | |  2  6  10 |   | x₂ |   | 0 |																											|
# | --         --   | x₃ |   -- --																											|
# |                 --  --																													|
# | 矩阵 A 的秩是 1, 行空间是 1 维的一条直线, 与之正交的零空间应该是 2 维中平面 ( n - r = 3 - 1 = 2 );													|
# | 零空间是垂直于行向量的平面, (1, 3, 5) 是这个平面的法向量, 由此得到平面方程: x₁ + 3x₂ + 5x₃;														|
