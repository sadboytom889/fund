# | 似然																																		|
# |																																			|
# | 概率: 利用已知的概率模型对随机事件发生的可能性大小的定量描述;																						|
# | 似然: 首先观察随机事件的结果再去推测概率模型;																									|
# |																																			|
# | 例: 抛硬币 100 次, 70 次正面朝上, 30 次反面朝上;																								|
# | 由于 70% 和 30% 是唯一的数据支撑, 因此猜测正面朝上的概率为 70%, 反面朝上的概率为 30%, 该过程就是似然;												|
# |																																			|
# | 似然函数: 该结果产生的概率的函数;																												|
# | l( p: 概率 ) = p⁷⁰ * ( 1 - p )³⁰;																										|
# |																																			|
# | 最大似然估计:																																|
# | 要推测 p 最好的估计应是使得似然函数值最大的某 p 值;																								|
# |																																			|
# | 求导: 当导函数取到 0 时就对应着似然函数的最大值,																								|
# | 因为 p 和 1 - p 都是小于 1 并且函数次数太高导致计算机计算的函数值过小引起数值下溢问题, 所以对似然函数取对数将连乘变成连加, 解决了计算问题也不会改变导函数的解	|
# | In( l( p ) ) = 70 * In( p ) + 30 * In( 1 - p );																							|
# | ( In( l( p ) ) )` = ( 70 * In( p ) + 30 * In( 1 - p ) )`;																				|
# |                   = ( 70 * In( p ) )` + ( 30 * In( 1 - p ) )`;																			|
# |                   = 70 / p + 30 / ( 1 - p ) * ( 1 - p )`;																				|
# |                   = 70 / p + 30 / ( 1 - p ) * -1;																						|
# |                   = 70 / p - 30 / ( 1 - p );																							|
# |																																			|
# | 代入函数值为 0: ( In( l( p ) ) )` = 0; 得到: p = 0.7;																						|
# | 所以概率 0.7 就是基于数据构建出似然函数的最优解;																								|
